                  Additionally, not only is act of reading inherently easier
                  than listening, but it’s also easier to learn from. It allows
                  for swift word look-ups with pop-up dictionaries and easy Anki
                  card creation through copy/pasting. In contrast, learning from
                  listening usually requires you to correctly hear words and
                  manually look them up.
                </p>
                <p>
                  Because of these advantages, focusing on reading is a highly
                  effective strategy for rapid improvement. What happens with
                  this strategy is that your reading ability ends up developing
                  past your listening ability, and then that reading ability is
                  subsequently transferred into listening ability.
                </p>
                <p>
                  When your reading ability develops past your listening
                  ability, catching your listening back up usually isn’t too
                  difficult. The reason for this is that learning to hear a
                  word/structure that you can already read is much easier than
                  learning a new word/structure entirely through listening. What
                  happens is that, as long as you continue to actively and
                  passively listen to the language, any reading ability you have
                  will eventually transfer over to listening ability.
                </p>
                <p>
                  Recall that in Stage 1 we recommended following a
                  listening-to-reading ratio of 1:1 for active immersion (i.e.,
                  dividing your active immersion time equally between the two
                  activities). This allowed the two abilities to work together
                  harmoniously, with reading informing your listening, and vice
                  versa. However, because reading ability is inherently easier
                  to develop than listening ability, some amount of gap should
                  have formed between the two by the end of Stage 1.
                </p>
                <p>
                  In order to utilize the strategy mentioned above, going
                  forward, we recommend maintaining this gap between reading and
                  listening ability. Do so by continuing to stick near the 1:1
                  listening-to-reading ratio, especially while you’re still near
                  the beginning of Stage 2.&nbsp;
                </p>
                <h3>
                  Disadvantages of a Large Gap Between Reading and Listening
                  Ability
                </h3>
                <p>
                  Although letting your reading ability develop past your
                  listening ability is a powerful strategy for rapid progress,
                  allowing the gap between the two to grow too large can have
                  negative consequences.&nbsp;
                </p>
                <p>
                  The first potential negative consequence of letting the gap
                  become too big is that active listening itself may become
                  tedious and painful.&nbsp;For example, let’s say someone
                  devoted most of their energy into reading while largely
                  neglecting active listening. This person would naturally end
                  up with highly developed ready ability and rather poor
                  listening ability. Once this happened, they would likely
                  become accustomed to understanding most of their immersion. In
                  turn, this may lead them to experience tedium and frustration
                  when faced with how little they understand when listening. We
                  can imagine them going on to avoid active listening
                  altogether, which would cause the gap to grow even larger.
                </p>
                <p>
                  The second potential negative consequence of letting the gap
                  between listening and reading ability grow too big is that it
                  may harm your pronunciation. This is because, even during
                  silent reading, you pronounce everything you read with a voice
                  inside your head (a.k.a.
                  <a
                    href="https://en.wikipedia.org/wiki/Subvocalization"
                    target="_blank"
                    rel="noopener"
                    >subvocalization</a
                  >).
                </p>
                <p>
                  The issue is that, before your listening abilities are fully
                  developed, it’s impossible to grasp the way a language should
                  sound. This means that, while reading raw text,&nbsp;your
                  subvocalization will inevitably be filled with
                  mispronunciations and unnatural intonation patterns. If you
                  read excessively without listening ability to back it up (in
                  other words,
                  <i>while you have a large listening/reading ability gap</i>),
                  these inaccuracies could lead to you internalize bad habits
                  that will manifest when you eventually start speaking your
                  target language.
                </p>
                <h3>
                  When Should I Be Worried about My Listening/Reading Ability
                  Gap?
                </h3>
                <p>
                  Our stance is that your gap is likely within a safe margin
                  when these two statements are true:
                </p>
                <ul>
                  <li>
                    Listening always comprises a significant portion of your
                    active immersion; and therefore, your listening ability is
                    <b>constantly improving</b>.
                  </li>
                  <li>
                    Your listening/reading ability gap doesn’t cause you
                    noticeable pain or frustration during active listening
                    immersion. If even just this statement is true, it’s
                    unlikely that your gap is too large.
                  </li>
                </ul>
                <p>
                  The key logic here is that if your listening ability is
                  constantly improving, then your <b>internal model</b> of how
                  the language sounds is being <b>regularly updated</b>. Due to
                  the constant updating, it’s unlikely that your subvocalized
                  pronunciation would become stagnant enough for ingrained
                  habits to form.
                </p>
                <p>
                  It is worth emphasizing that if listening doesn’t make up some
                  significant portion of your active immersion, then the margin
                  of safety will <b>decrease </b>in your case due to the logic
                  stated above.
                </p>
                <h3>Conclusion: Listening Ability vs. Reading Ability</h3>
                <p>
                  To summarize, letting your listening ability lag behind your
                  reading ability is generally the most effective strategy for
                  rapid improvement. That said, it’s important not to neglect
                  listening throughout the process, as letting your listening
                  ability get too far behind has negative consequences.
                </p>
                <p>
                  Over time, you will naturally develop an intuition for what
                  the ideal gap size is between listening and reading ability.
                  Once this begins to happen, you can start regularly shifting
                  your listening-to reading ratio in order to maintain that
                  ideal gap size. For example, if you feel your listening
                  ability is starting to lag a little too far behind your
                  reading ability, you can decide to decrease your reading time
                  and increase your active listening time for the following few
                  weeks.&nbsp;
                </p>
                <h2 id="subtitles">Subtitles</h2>
                <h3>NL (Native Language) Subs</h3>
                <p>
                  In Stage 1, we said that it’s fine to start immersing in your
                  target language with NL subs (native language subtitles).
                  Because jumping straight into immersion can initially feel
                  jarring and intimidating, watching with NL subs can be a
                  useful way to get accustomed to spending time with your target
                  language.
                </p>
                <p>
                  With that said, immersing with NL subs is
                  <b>relatively low quality immersion</b>, equivalent to passive
                  listening at best. Put simply, if you’re reading subtitles,
                  then you’re not fully paying attention to the audio. This is
                  why it’s important to wean yourself off of NL subs once you
                  have gotten into the habit of immersing yourself in your
                  target language. If you haven’t already, we recommend fully
                  weaning yourself off NL subs before starting Stage 2.
                </p>
                <p>
                  Some people take the strategy of generally watching without
                  subtitles, and pausing to look at NL subs only when they don’t
                  understand something or want to double check. This technique
                  is clearly a large step up from immersing with NL subs on the
                  entire time, as the amount of time you spend in unmediated
                  contact with your target language is vastly increased. That
                  being said, it still comes with some of its own problems.
                </p>
                <p>
                  The first of these is that looking at NL subs will
                  <b>bias the way you interpret your target language</b>. No
                  matter how skillful the translator, ultimately, every
                  translation is imperfect. Every language contains unique
                  nuances and subtleties that simply cannot be fully translated
                  into another language. Additionally, translators often
                  prioritize creating subtitles that are easy to follow for the
                  intended audience, rather than 1:1 translations of meaning.
                </p>
                <p>
                  Because of this, reading NL subs may lead you to draw
                  <b>false equivalencies</b> between words, structures, and
                  phrases. For example, you might pick out a target language
                  word in your immersion and equate it to a word you saw in the
                  NL subtitles, when in reality the two have slightly different
                  nuances. Once internalized, these sorts of false equivalencies
                  can be hard to overcome, and can lead to using the language in
                  unnatural ways later down the line.
                </p>
                <p>
                  The other, less obvious, pitfall of using NL subs to clear up
                  confusion is that it can interfere with cultivating a
                  <b>tolerance for ambiguity</b>. “Tolerating ambiguity” means
                  being okay with not fully understanding the plot of a show or
                  the meaning of a word or structure. It means being able to
                  trust the process, knowing that blurry understandings will
                  naturally become clear over time.
                </p>
                <p>
                  Growing your capacity to tolerate ambiguity is essential to
                  making rapid progress. Unfortunately, tolerating ambiguity
                  often doesn’t come naturally. Most of us have grown up in a
                  culture that places a lot of value on “understanding things.”
                  Because of this, for most people, being able to tolerate
                  ambiguity is a skill that must be actively cultivated over
                  time. And it’s not hard to see how looking at NL subs every
                  time you don’t understand something slows down this
                  cultivation.
                </p>
                <p>
                  Although watching with NL subs is something that you should
                  have completely left behind before moving into Stage 2, in the
                  initial phase of Stage 2, it’s okay to use the strategy of
                  turning them on only when you don’t understand something. But,
                  try to rely on NL subs as little as possible, and slowly wean
                  yourself off using them in any form as you progress through
                  Stage 2.
                </p>
                <p>
                  The same advice goes for bilingual texts and any other content
                  that revolves around learning through translated material. One
                  of the main goals of Stage 2 is to become able to learn your
                  target language
                  <strong>entirely in your target language</strong>, without
                  using your native language as a crutch.
                </p>
                <h3>TL (Target Language) Subtitles</h3>
                <p>
                  On the other hand, we also have TL subs (target language
                  subtitles). In Stage 1, we recommended watching TL subbed
                  content as an effective way to become acquainted with the
                  written form of your target language. When you’re a beginner
                  and don’t yet know a lot of words, audio-visual mediums are
                  inherently more comprehensible than raw text mediums like
                  books or articles. TL subbed content lets you take advantage
                  of the comprehensibility of audio-visual mediums to help get
                  your foot in the door of reading.
                </p>
                <p>
                  Though we often consider watching TL subbed content as a form
                  of reading, ultimately, is a hybrid of both reading and
                  listening. When watching with TL subs, your reading ability
                  helps fill in gaps in your listening ability, and vice versa.
                  Because of this, you’re likely to understand significantly
                  more than when either watching with no subs, or when reading
                  raw text. This is great in the beginning of the language
                  acquisition process; when you first start out, understanding
                  anything at all is extremely difficult, and you need all the
                  help you can get. That said, once you start to become more
                  advanced, watching with TL subs can become a crutch.
                </p>
                <p>
                  Something we explained in Stage 1 is that because watching
                  with TL subs is closer to reading than listening, in order to
                  fully develop your listening skills,
                  <b
                    >it’s important to spend large amounts of time actively
                    listening without any subtitles</b
                  >. The reason for this is that the hardest part of listening
                  comprehension is determining what words are being spoken from
                  sound alone. When watching with TL subs, what words are being
                  spoken are presented to you right there on the screen. Thus,
                  watching with TL subs doesn’t provide your brain with an
                  opportunity to practice that crucial skill of determining what
                  words are being said just from sound.
                </p>
                <p>
                  Ultimately, the same is true for reading as well. When
                  listening to spoken language, intonation and stress make it
                  relatively easy to parse the grammar of what is being said. On
                  the other hand, when reading, you don’t have any intonation to
                  rely on, and this can make parsing the grammar much more
                  difficult.
                </p>
                <p>For example, take the following sentence:</p>
                <blockquote class="bq">
                  <p>
                    <a
                      href="https://en.wikipedia.org/wiki/Garden-path_sentence#Examples"
                      target="_blank"
                      rel="noopener"
                      >“The complex houses married and single soldiers and their
                      families.”</a
                    >
                  </p>
                </blockquote>
                <p>
                  If this sentence was spoken out loud, the intonation would
                  make it easy to understand. But in written form, figuring out
                  how to parse the grammar is quite difficult.
                </p>
                <p>
                  If the only reading practice you got is watching TL subbed
                  content, you would never get practice parsing syntax without
                  the help of intonation. Because of this, as you progress
                  through Stage 2,
                  <b
                    >we recommend slowly replacing watching TL subbed content
                    with reading raw text.</b
                  >
                </p>
                <p>
                  In real life, most everyday linguistic situations require us
                  to be able to listen without relying on our eyes, and read
                  without relying on our ears. Because of this, the most
                  efficient way to meaningfully grow your language ability is to
                  practice doing just that:
                  <b
                    >listening without a transcript, and reading without
                    audio.</b
                  >
                </p>
                <h2 id="reading">Reading</h2>
                <h3>Reading Raw Text Is Difficult</h3>
                <p>
                  As explained above, once you transition into Stage 2, we
                  recommend gradually moving away from watching TL subbed
                  content, <b>towards reading raw text</b> (fully text-based
                  mediums) instead. But, breaking into reading raw text can
                  initially be quite intimidating and challenging.
                </p>
                <p>
                  First of all, there is the issue mentioned earlier of having
                  to parse grammar without any help from intonation patterns.
                  This is a skill that takes practice to develop, and will
                  likely be challenging at first.
                </p>
                <p>
                  Second, when reading raw text, because you don’t have any
                  visual or auditory clues to help you, you have nothing but
                  your language ability to rely on. This means that what is
                  required to reach a basic level of comprehension is
                  significantly higher.
                </p>
                <p>
                  Third, when compared to watching with TL subs, reading raw
                  text makes what you <i>don’t know</i> much more distracting
                  and in-your-face. This is because the pacing of audio/visual
                  media is quick and out of your control. In contrast, you can
                  read raw text at any pace you’d like. This makes letting go
                  and tolerating ambiguity much more difficult. As you have
                  probably experienced, it is often very tempting to look up
                  every unknown word or grammar structure in a difficult
                  sentence.
                </p>
                <p>
                  And lastly, in most languages the range of vocabulary used in
                  writing is usually significantly larger than that of spoken
                  language. For example, take the English words “astonishing,”
                  “astounding,” “spectacular,” “phenomenal,” “miraculous,” and
                  “breathtaking.” These are all words that appear relatively
                  frequently in formal written English. Although these words are
                  occasionally used in spoken English as well, within the
                  context of normal everyday conversation, it’s much more common
                  to simply say “awesome” or “cool.”
                </p>
                <p>
                  When watching content with TL subs, you’re essentially
                  exposing yourself to
                  <b>a written form of the spoken language</b>. Because of this,
                  when you initially make the jump from watching TL subbed
                  content to reading raw text, you’re likely to find that the
                  percentage of unknown words dramatically increases. This is
                  perhaps the largest obstacle of learning to read raw text.
                </p>
                <p>
                  So, although we argue that ultimately reading is easier than
                  listening, becoming proficient in reading raw text comes with
                  its own set of unique challenges. Being aware of these
                  challenges, and setting your expectations accordingly, is one
                  of the keys to successfully breaking into raw text.
                </p>
                <h3>Avoid Feeling You Need to Be “Ready”</h3>
                <p>
                  Here is a trap that many people fall into. Coming out of Stage
                  1, their immersion consists of solely audio and audio/visual
                  content, with and without TL subs. They have reached a point
                  where they’re relatively comfortable with certain TL subbed
                  content, and decide it might be time to transition into raw
                  text. So, they pick up a book they have always wanted to read
                  and jump right in.
                </p>
                <p>
                  They are then immediately confronted with the brute reality of
                  how much more difficult books are. This causes them to quickly
                  give up and return to audio/visual content, concluding that
                  “they aren’t ready for reading books yet.” After a few more
                  months of immersing with audio/visual content, they eventually
                  try the book again, only to find it just as inapproachable as
                  before. They end up repeating this process indefinitely,
                  perpetually postponing the jump into reading raw text.
                </p>
                <p>
                  The problem here is that, in a certain sense, you will never
                  be “ready” for raw text until <strong>after </strong>you have
                  spent a significant amount of time grappling with it. In other
                  words, if you wait until you can <i>already </i>ride a bike
                  before taking off the training wheels, you’ll never become
                  able to ride a bike; the process of learning to ride a bike
                  begins <strong>after</strong> the training wheels are removed.
                </p>
                <p>
                  The challenges of reading that were explored above are only
                  found within raw text, so the only way to build the skills
                  necessary to overcome them is through
                  <strong>actually reading raw text</strong>. No matter how long
                  you wait, the first book you ever read will always be
                  extremely difficult. But, after finishing that first book, the
                  next one won’t feel nearly as difficult.
                </p>
                <p>
                  Now, to a certain extent, it’s most definitely true that it’s
                  important to have a solid foundation in the language before
                  venturing into raw text. So, in many cases, concluding that
                  you aren’t ready yet may indeed be the right decision. But,
                  keep in mind that the foundation necessary to jump into raw
                  text is likely much less than you initially expect,&nbsp;and
                  be wary of falling into the trap mentioned above.
                </p>
                <h3>Getting Started with Raw Text</h3>
                <p>
                  When getting started with raw text, there is no need to go
                  from 0-to-100 . Many of the struggles stated above can be
                  avoided by easing your way into raw text in a strategic way.
                </p>
                <p>
                  First of all, we recommend starting off with content that
                  contains grammar and vocabulary similar to that of spoken
                  language. Some good examples are: YouTube comments, blogs,
                  comic books/graphic novels/manga, and movie/TV show
                  transcripts.
                </p>
                <p>
                  Another good option is TL subs: if you have access to TL
                  subtitle files, you can treat them as raw text and read them
                  on their own. The
                  <a
                    href="https://subtitletools.com/convert-subtitles-to-plain-text-online"
                    target="_blank"
                    rel="noopener"
                    >Subtitle Tools website</a
                  >
                  can come in handy here as it allows you to convert subtitle
                  files into plain text. Consider reading the transcripts of
                  shows that you have already seen before; because you already
                  know the basic plot, it will be easier to follow the text.
                </p>
                <p>
                  When initially approaching raw text, it may be a good idea to
                  start with something as simple as spending 5 minutes a day
                  attempting to understand the comments on a target language
                  YouTube video. As you get more comfortable, you can gradually
                  increase the amount of time you spend reading raw text each
                  day.&nbsp;
                </p>
                <p>
                  As your skills develop, try to move from things like comic
                  books and blogs towards more challenging content, such as news
                  articles or text-heavy video games.
                </p>
                <p>
                  Eventually, by the end of Stage 2, your goal should be to
                  tackle the holy grail of reading: books. Books, both
                  nonfiction and fiction, comprise the highest possible quality
                  of reading immersion.
