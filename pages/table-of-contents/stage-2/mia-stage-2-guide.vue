                <h2 id="listening">Listening</h2>
                <h3>Comprehensibility</h3>
                <p>
                  For the bulk of Stage 1, the goal of listening was to adjust
                  your ears to the sound system of your target language, and
                  begin learning to pick out and understand the most common
                  words and phrases.
                </p>
                <p>
                  Regardless of the specific domain, the sound system and core
                  fundamental vocabulary is present in virtually every form of
                  the spoken language.&nbsp;For example, no matter whether
                  you’re watching a horror movie or listening to the news, all
                  spoken language is made up of the same vowels and consonants,
                  as well as the same basic words, such as “I,” “him,” “there,”
                  and “go” in English. Because of this, when you first start out
                  learning a language, you’re likely to understand a similar
                  amount regardless of what type of content you’re listening to.
                  Because of this, choosing the right immersion content wasn’t
                  important at the time.
                </p>
                <p>
                  But, by the time you reach Stage 2, you will have built up
                  enough of a foundation in your target language to where,
                  depending on the content, it’s actually possible to understand
                  a significant amount while listening. Because of this, it
                  starts to make sense to prioritize comprehensibility when
                  picking immersion material. Let’s go into why.
                </p>
                <h3>Why Comprehensibility is Prioritized</h3>
                <p>
                  Once you hit a critical mass of understanding, learning from
                  immersion becomes significantly easier. For example, let’s say
                  that you’re able to understand around 80% of a given sentence.
                  Assuming that you know the details of the context in which the
                  sentence was spoken, it usually isn’t difficult to fill in the
                  last 20%.
                </p>
                <p>
                  To demonstrate, let’s say you’re watching the following scene:
                  someone walks into a room where another person is playing a
                  guitar and says, “I thought I told you not to **** my ****!”
                  It would be pretty clear that the first “****” must mean
                  “play,” and the second “****” must mean “guitar.” It would be
                  especially easy to draw this connection if you already knew
                  the words “play” and “guitar” through reading, but simply
                  weren’t able to hear them in native speech yet. In this case,
                  after picking out these words from audio alone for the first
                  time, you are likely to become able to pick them out more
                  easily in the future; even in more ambiguous contexts.
                </p>
                <p>
                  We might call this type of sentence an “<b
                    >optimal growth sentence</b
                  >.” The more optimal growth sentences that a piece of content
                  contains, the more consuming that content will grow your
                  ability.
                </p>
                <p>
                  For most of Stage 1, for the reasons explained previously, you
                  are likely to find a similar number of optimal growth
                  sentences regardless of the specific type of content.
                </p>
                <p>
                  But,
                  <b
                    >once you begin approaching Stage 2, you are likely to find
                    certain types of content to contain significantly more
                    optimal growth sentences</b
                  >. Once you reach this stage, focusing most of your active
                  listening on this highly comprehensible content will yield
                  very rapid progress.
                </p>
                <p>
                  It’s important to note that the term “comprehensibility” can
                  be thought of <b>ineffectively</b>. Thinking about an entire
                  piece of content as being either “comprehensible” or
                  “incomprehensible” usually isn’t very helpful. Instead, using
                  the framework of “<b
                    >how many of the <i>sentences </i>within this piece of
                    content are optimal growth sentences</b
                  >?” is much more useful.
                </p>
                <p>
                  For example, even if a show is largely incomprehensible as a
                  whole, if you end up hearing around one optimal growth
                  sentence a minute, then watching that show will greatly grow
                  your listening ability. When looked at this way,
                  comprehensibility can be seen as a spectrum, rather than a
                  black-or-white judgement.
                </p>
                <h3>
                  How to Choose Immersion Content with Comprehensibility in
                  Mind.
                </h3>
                <p>
                  When talking about breaking into books, we mentioned the
                  time-tested strategy of reading multiple books by the same
                  author as a way to quickly gain a foundation. We can apply a
                  similar strategy to understanding spoken language as well.
                  Listening to content that is largely from the same
                  medium/genre for an extended period of time will lead to you
                  getting familiar with the
                  <b>specific vocabulary and structures</b> commonly used in
                  that domain.
                </p>
                <p>
                  Through this, the comprehensibility of that domain of content
                  will drastically rise, providing you with a platform for
                  accelerating your listening ability. Once you become
                  thoroughly familiar with one domain of speech, branching out
                  to other domains won’t be difficult either.
                </p>
                <p>
                  Additionally, certain mediums and genres are inherently more
                  comprehensible than others. On the level of mediums,<b>
                    any content with a visual component usually has at least a
                    basic level of comprehensibility</b
                  >. Also, fictional content with an ongoing narrative, such as
                  TV dramas, tend to have high levels of comprehensibility.
                  Since you’ll have familiarity with the characters and the
                  world of the story, you’ll possess an immense amount of
                  context which will help you interpret the plot and dialogue.
                </p>
                <p>
                  Lastly, when it comes to genres, realistic fiction tends to be
                  significantly more comprehensible than fantasy or sci fi, due
                  to the narrower range of vocabulary used.
                </p>
                <h3>The Nitty-Gritty of Listening</h3>
                <p>
                  When it comes to listening, admittedly not much changes
                  between Stage 1 and Stage 2. All of the recommendations
                  related to how to immerse and what to immerse with still hold.
                </p>
                <p>
