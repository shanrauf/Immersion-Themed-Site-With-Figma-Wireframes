                <h1 id="stage-2">Stage 2 at a Glance</h1>
                <p>
                  Stage 2 is largely a continuation of Stage 1. In Stage 1, you
                  started off by learning the basic phonology, writing system,
                  grammar, and vocabulary of your target language. You then
                  transitioned to learning directly from native media in the
                  form of sentence cards. Of course, this was all alongside
                  immersing yourself in native content in order to integrate
                  what you consciously learned and begin internalizing the
                  language on an unconscious level.
                </p>
                <p>
                  This was all for the purpose of building a foundation in
                  <i>understanding </i>your target language. In Stage 2, you’ll
                  continue to increase your capacity to understand by doing, for
                  the most part, more of the same: immersion and sentence
                  mining. The nature of how you immerse and the strategies you
                  employ to mine sentences will shift, but the underlying
                  structure will stay the same. The more words you learn and the
                  more quality time you spend with your target language, the
                  more your comprehension will grow.
                </p>
                <p>
                  One major point of Stage 2 is further developing your reading
                  skill, with the goal of moving into reading raw text. The
                  <b>Immersion </b>and <b>SRS </b>sections will help you
                  accomplish this.
                </p>
                <p>
                  There is also the monolingual transition, perhaps the most
                  notable change that Stage 2 brings. The monolingual transition
                  is the process of transitioning to learning your target
                  language <i>in your target language</i>, without relying on
                  your native language. This transition will radically
                  accelerate the growth rate of your comprehension. The
                  <b>Monolingual Transition</b> section will explain everything
                  you need to know to make the transition.
                </p>
                <p>
                  By the end of Stage 2, you should be able to understand enough
                  of your target language to comfortably survive in a country
                  where the language is spoken. There should be some basic
                  content, such as slice-of-life TV shows or internet blogs,
                  that you’re close to being able to fully understand. This
                  opens up the possibility of learning to speak and write the
                  language in a natural way, which will be explored in Stage 3.
                </p>
                <h3>How Do I Know I’m Ready for Stage 2?</h3>
                <p>Here are some basic criteria:</p>
                <ul>
                  <li>
                    You have a firm grasp of your target language’s phonology
                    and writing system.
                  </li>
                  <li>You know at least ~3,000 words.</li>
                  <li>
                    No matter what you are listening to, you can usually
                    understand at least one word in every sentence spoken.
                  </li>
                  <li>
                    When listening to basic spoken content (such as talking-head
                    YouTube videos or slice-of-life TV shows) you can usually
                    understand at least 50% of the words in most sentences, and
                    fully understand a longer sentence around once every few
                    minutes.
                  </li>
                  <li>
                    When consuming basic written content (such as blogs or
                    simple TV shows w/ TL subs) you’re often able to follow the
                    basic ideas being expressed (although you may miss many
                    details).
                  </li>
                </ul>
                <p>
                  That said, ultimately, it’s not important to make a razor
                  sharp distinction between Stage 1 and Stage 2. Regardless of
                  the criteria listed above, feel free to begin gradually
                  transitioning to Stage 2 whenever you feel it might be a good
                  idea to do so. If you find yourself struggling with a Stage 2
                  technique, simply return to the Stage 1 equivalent and try
                  again in a month or two. There’s no harm in trying.
                </p>
                <h1 id="immersion">Immersion</h1>
                <p>
                  Although immersion is constant throughout every Stage of MIA,
                  the specifics of how you immerse will vary as you move from
                  stage to stage. During Stage 2, you will begin introducing raw
                  text into your immersion. At this point, understanding the
                  relationship between listening ability and reading ability
                  becomes important.&nbsp;
                </p>
                <h2 id="listening-vs-reading">
                  Listening Ability vs. Reading Ability; and Why Reading is
                  Prioritized
                </h2>
                <p>
                  When it comes to understanding a foreign language, listening
                  comprehension is generally much harder than reading
                  comprehension. This is because:
                </p>
                <ul>
                  <li>
                    You need to be able to process language at the rate of
                    speech.
                  </li>
                  <li>
                    Native pronunciation tends to be inconsistent and mumbly.
                  </li>
                </ul>
                <p>
                  On the other hand, when reading, you can take as long as you
                  like, and written words appear in consistent and predictable
                  forms.
                </p>
