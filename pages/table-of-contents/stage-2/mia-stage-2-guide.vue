                  they understand enough of their target language for background
                  listening to become extremely distracting. Remember that
                  background listening refers to keeping a form of your target
                  language on in the background and mostly ignoring it, while
                  focusing on other mentally intensive tasks. If you begin to
                  experience this problem,
                  <b>you may try using inherently less distracting content</b>,
                  such as audiobooks.
                </p>
                <p>
                  Otherwise, it’s also perfectly fine to drop background
                  immersion altogether if it’s distracting for you. However, we
                  do encourage you to continue taking advantage of passive
                  immersion, as it can drastically increase the amount of time
                  you spend with your target language. It is still a high enough
                  quality of immersion to, when combined with active immersion,
                  significantly contribute to your speed of progress.
                </p>
                <p>
                  On another note, once a gap forms between your listening and
                  reading ability, listening largely becomes a matter of
                  catching up to your reading ability. In a way, this makes
                  active listening extremely straightforward, as you’re simply
                  trying to auditorily pick out as much of what you can already
                  read as possible.
                </p>
                <p>
                  Also: when listening, at times when your overall comprehension
                  is low, try to focus on picking out as many words as possible.
                  When your comprehension is higher, try to focus on
                  understanding the main gist of as many sentences as you can.
                </p>
                <p>
                  <b
                    >Don’t get frustrated when you fail to hear words or phrases
                    that you can easily read</b
                  >. This is simply par for the course. As you continue to
                  actively and passively listen, the amount that you’re able to
                  understand will continue to grow. It really all boils down to
                  the number of hours you spend trying to understand the spoken
                  language.
                </p>
                <h1 id="monolingual-transition">Monolingual Transition</h1>
                <p>
                  When moving from Stage 1 to Stage 2, the most significant
                  change that will take place in your learning process will be
                  the monolingual transition.
                </p>
                <p>
                  For the duration of Stage 1 and the initial portion of Stage
                  2, when you wanted to learn a word in your target language,
                  you looked up the word in a bilingual dictionary (a type of
                  dictionary that translates words between two languages). In
                  this case, it was between your native language and your target
                  language.
                  <b
                    >In contrast, a monolingual dictionary defines the words of
                    a language with the words from that</b
                  >
                  language; for example, an English-to-English dictionary.
                </p>
                <p>
                  The monolingual transition is the process of transitioning to
                  using monolingual dictionaries, which enables you to begin
                  learning your target language entirely in your target
                  language. Making the monolingual transition is also referred
                  to as <b>“going monolingual.”</b>
                </p>
                <p>
                  This breaking away from relying on your native language as a
                  crutch to learn your target language comes with many powerful
                  benefits. And it represents a significant landmark on the path
                  to fluency.
                </p>
                <h2 id="reasons-monolingual">Reasons to go Monolingual</h2>
                <p>
                  Broadly speaking, there are four main benefits to making the
                  monolingual transition.
                </p>
                <h3>Reason #1</h3>
                <p>
                  Firstly, you will be able to
                  <b
                    >avoid creating false associations between words in your
                    native language and words in your target language</b
                  >. This idea was briefly touched on earlier in this guide (in
                  the section on dropping native language subs).
                </p>
                <p>
                  Basically, it’s very rare for a word in one language to have
                  an exact, one-to-one correlate in another language. For
                  example, take the Japanese word “warui” (悪い、わるい). The
                  most common English translation of the word “warui” is “bad.”
                  But in reality, “warui” is used quite differently than “bad”
                  in ways that often aren’t interchangeable with each other.
                </p>
                <p>
                  For example, in English, if someone has a low level of ability
                  in a skill, we say that they’re “bad” at it; e.g., “I’m bad at
                  playing the piano.” Thus, if you equated “warui” with “bad,”
                  you might assume that the Japanese sentence
                  “料理をしたのが悪かった” means “I was bad at cooking.”
                </p>
                <p>
                  In reality, it means something along the lines of, “Me having
                  cooked was a mistake.” This is because “warui” is never used
                  to describe a low level of ability. Rather, when being used in
                  reference to an action, it usually expresses something being
                  “bad” in the sense of leading to a bad end result for the
                  parties involved.
                </p>
                <p>
                  In this specific scenario, in order to stop misconstruing
                  sentences that contain “warui,” somewhere along the line you
                  need to <i>un</i>learn that it can mean “low level of
                  ability”; it’s a false assumption you made as a consequence of
                  equating it with the word “bad.” Now, as long as you continued
                  to immerse, sooner or later this misunderstanding would become
                  apparent to you and get cleared up. But, until that happens,
                  it will cause confusion and interruption to your learning
                  process.
                </p>
                <p>
                  If you had instead learned “warui” using a Japanese
                  monolingual dictionary, then the false connection would have
                  never been made in your head in the first place. You would
                  have bypassed the entire process of mislearning and
                  unlearning. It’s not hard to understand how this would raise
                  the overall efficiency of your learning process.
                </p>
                <p>
                  But because there are close to no true one-to-one correlates
                  between languages,
                  <b
                    >these sorts of false associations happen all the time on
                    more subtle levels as well</b
                  >. Even if you aren’t outright misunderstanding the meaning of
                  a target language word, due to associating it with a word from
                  another language, you may subtly misunderstand the nuances or
                  connotations it has in certain situations.
                </p>
                <p>
                  This is why completely decoupling your knowledge of your
                  target language from other languages is so useful. It allows
                  you to begin building a web of understanding that exists and
                  grows within its own closed system, without being constrained
                  by outside influences. At any rate, we will return to this
                  idea later when we look at the third reason that going
                  monolingual is beneficial.
                </p>
                <h3>Reason #2</h3>
                <p>
                  The second benefit of using a monolingual dictionary is that
                  it
                  <b
                    >reduces the amount of time it takes to fully comprehend
                    words</b
                  >. Understanding a word deeply enough to use it in the same
                  way as native speakers usually requires more than simply
                  understanding the technical meaning.
                </p>
                <p>
                  For example, consider the difference between the word “hard”
                  and the word “difficult.” In a technical sense, the sentences
                  “calculus is hard” and “calculus is difficult” mean exactly
                  the same thing. But, if you’re fluent in English, you know
                  each of these two sentences <i>feel </i>slightly different.
                  “Difficult” feels slightly more formal and proper, while
                  “hard” feels more casual and laid-back.
                </p>
                <p>
                  However, it is practically impossible to accurately explain
                  the exact differences to someone who has never heard these
                  words before. The only way to truly grasp the full nuance is
                  to hear each of them countless times within many different
                  contexts and phrases. In this way, even monolingual
                  definitions have their limits when it comes to fully acquiring
                  words; because
                  <b>many words are to some degree undefinable</b> (i.e.,
                  difficult to pin down with a brief description). At the end of
                  the day, <b>immersion </b>is always going to be necessary in
                  order to fully master a given word.
                </p>
                <p>
                  With that said, in general, monolingual definitions are able
                  to take you much closer to the true nuance of a word than a
                  bilingual definition can. The reason for this is that, while
                  bilingual definitions tend to simply provide a list of similar
                  words from another language,
                  <b
                    >monolingual definitions actually attempt to articulate the
                    essence of the word.</b
                  >
                </p>
                <p>
                  For example, take the Japanese word “tanjun” (単純,
                  たんじゅん). The online English-Japanese dictionary
                  <a href="https://jisho.org/" target="_blank" rel="noopener"
                    >Jisho</a
                  >
                  defines the word “tanjun” as:
                </p>
                <blockquote class="bq">
                  <p>“simple; plain; uncomplicated; straightforward.”</p>
                </blockquote>
                <p>
                  In the Japanese monolingual dictionary Daijirin (大辞林),
                  “tanjun” is defined as:
                </p>
                <ol>
                  <li>
                    <blockquote class="bq">
                      <ol>
                        <li>
                          こみいった点がなく簡単な・こと（さま）。⇔複雑「―な構造」「―なミス」
                        </li>
                        <li>
                          考え方などが一面的で行き届かない・こと（さま）。「―な発想」「―な頭の働き」
                        </li>
                      </ol>
                    </blockquote>
                  </li>
                  <li>
                    <blockquote class="bq">
                      <p>
                        他種のものがまざっていない・こと（さま）。純一。「―泉」
                      </p>
                    </blockquote>
                  </li>
                  <li>
                    <blockquote class="bq">
                      <p>制限や条件のない・こと（さま）。「―承認」</p>
                    </blockquote>
                  </li>
                </ol>
                <p>Here is our translation of the definition:</p>
                <ol>
                  <li>
                    <blockquote class="bq">
                      <ol>
                        <li>
                          To be simple and have no points of complication.
                          Opposite of “complicated.” “A ‘tanjun’ structure.” “A
                          ‘tanjun’ mistake.”
                        </li>
                        <li>
                          For a way of thinking to be shortsighted and lacking
                          thoroughness. “A ‘tanjun’ idea.” “A ‘tanjun’
                          psychological process.”
                        </li>
                      </ol>
                    </blockquote>
                  </li>
                  <li>
                    <blockquote class="bq">
                      <p>
                        To not have other types of things mixed in. Pure. “A
                        ‘tanjun’ hot spring.”
                      </p>
                    </blockquote>
                  </li>
                  <li>
                    <blockquote class="bq">
                      <p>
                        To have no limitations or conditions. “‘Tanjun’
                        acceptance”
                      </p>
                    </blockquote>
                  </li>
                </ol>
                <p>
                  We can see that the monolingual definitions give a much more
                  in-depth idea of the exact meaning and usage of the word
                  “tanjun.” Simply by reading the bilingual definition, most
                  people probably wouldn’t assume that “tanjun” could be used as
                  a way to insult someone for lacking critical thinking ability.
                  Yet, this comes across very clearly in the monolingual
                  definition.
                </p>
                <p>
                  So, in general, monolingual definitions bring you much closer
                  to a complete understanding of words than bilingual
                  definitions do. This means that there will be significantly
                  less gap for you to bridge in order to fully understand words;
                  which in turn makes your overall learning process that much
                  more efficient.
                </p>
                <h3>Reason #3</h3>
                <p>
                  The third benefit of using a monolingual dictionary is that it
                  helps to
                  <b
                    >accelerate the process of learning to think in your target
                    language.</b
                  >
                </p>
                <p>
                  Normally, when trying to infer or recall the meaning of a word
                  in your target language, you do so by conjuring up equivalents
                  from your native language. But, once you’re able to function
                  monolingually, you’ll be able to contemplate words entirely
                  within the domain of your target language.
                </p>
                <p>
                  Take for example the scenario of reviewing Anki cards before
                  having gone monolingual. You’ll likely test your memory of a
                  target word’s meaning by recalling equivalents from your
                  native language. In contrast, after going monolingual, you’ll
                  be able to test yourself by recalling an approximation of the
                  monolingual definition.
                </p>
                <p>
                  If language is a puzzle, then the monolingual transition leads
                  to a huge increase of intimacy with the puzzle pieces. As
                  such, it marks a big step towards learning to think in your
                  target language.
                </p>
                <p>
                  On a side note, editors put a lot of thought into how
                  dictionary definitions should be written. Through this as
                  well, you will gain further insight into how native speakers
                  think.
                </p>
                <p>
                  As we touched on previously,
                  <b
                    >the potential to describe a language in terms of another is
                    limited</b
                  >. On the other hand, describing words within its own language
                  is limitless. It allows you to make direct references to other
                  words and concepts from that language. As a result, your
                  accuracy and precision with the meaning of words will
                  increase; for example, your ability to notice the subtle
                  differences between synonyms will increase massively.
                </p>
                <p>
                  One side benefit of learning to think in your target language
                  occurs whenever you are speaking with native speakers. That
                  is, whenever you don’t know or remember a word, you will be
                  able to describe the concept in terms of other words (and in a
                  way that the native speaker is very likely to understand). As
                  a language learner, this is extremely valuable.
                </p>
                <h3>Reason #4</h3>
                <p>
                  And the last, rather simple benefit of going monolingual, is
                  that it will
                  <b>increase the time you spend with your target language</b>.
                  As a language learner, you’re destined to frequently look up
                  words and phrases (and the occasional grammar structure).
                  Before going monolingual, all of this is done in your native
                  language. But once you go monolingual, all of that time will
                  be spent with target language definitions and explanations.
                  Even if it’s not much, this still means spending more time
                  overall with your target language. This additional time can
                  add up in the long term, thus increasing the speed at which
                  you improve.
                </p>
                <h2 id="challenges-monolingual">
                  The Challenges of Going Monolingual
                </h2>
                <p>
                  There are two hurdles that must be overcome to become able to
                  understand monolingual definitions.
                </p>
                <p>
                  The first of these is overcoming what we call
                  <b>“dictionary vocabulary.”</b> These are words that show up
                  rather infrequently within most types of content, yet appear
                  frequently inside monolingual definitions. Their essential
                  function is to allow definitions of abstract concepts to be
                  expressed precisely. As such, they tend to not lend themselves
                  very well to the purposes of everyday conversation.
                </p>
                <p>
                  For example, here is
                  <a
                    href="https://www.merriam-webster.com/dictionary/cut"
                    target="_blank"
                    rel="noopener"
                    >Merriam-Webster’s first definition for “cut”</a
                  >:
                </p>
                <blockquote class="bq">
                  <p>“to penetrate with or as if with an edged instrument.”</p>
                </blockquote>
                <p>
                  Now, since you’re reading this guide, chances are you don’t
                  have any trouble understanding this definition. However, it’s
                  not hard to imagine that if someone was near Stage 2 of using
                  MIA to acquire English, they likely wouldn’t yet know the word
                  “penetrate,” or the fact that “instrument” can refer to tools.
                  These are words that an English learner may not learn through
                  general immersion until Stage 3 or 4. Although they show up
                  infrequently in contexts like TV or daily conversation, these
                  words come up quite frequently within the dictionary.
                </p>
                <p>
                  If you postponed going monolingual until you had learned all
                  of this “dictionary vocabulary” from general immersion, you
                  would likely end up waiting until Stage 4 to make the
                  transition.
                </p>
                <p>
                  But, in truth,
                  <b
                    >the amount of “dictionary vocabulary” is actually quite
                    small</b
                  >. We estimate that, depending on the language and specific
                  dictionary, there are around 300 to 500 of them. In other
                  words, by learning these specific 300 to 500 words going
                  monolingual in Stage 2 becomes completely accessible. We will
                  talk about <b>how to go about learning</b> this “dictionary
                  vocabulary” in <b>the next section.</b>
                </p>
                <p>
                  The other hurdle that must be overcome in order to go
                  monolingual is getting used to the
                  <b
                    >abstract and self-referential writing style of
                    definitions</b
                  >. Furthermore, due to the abstract yet technical nature of
                  defining words, definitions frequently contain grammar that is
                  difficult to understand.
                </p>
                <p>
                  For example, clauses may be nested within clauses that are
                  also nested within other clauses; or one abstract concept will
                  be described by a combination of two other abstract concepts.
                  All of this can make monolingual definitions fairly difficult
                  to wrap your head around.
                </p>
                <p>
                  To give a concrete illustration, here is
                  <a
                    href="https://www.merriam-webster.com/dictionary/experience"
                    target="_blank"
                    rel="noopener"
                    >Merriam-Webster’s second definition for “experience”</a
                  >:
                </p>
                <blockquote class="bq">
                  <p>
                    “the fact or state of having been affected by or gained
                    knowledge through direct observation or participation.”
                  </p>
                </blockquote>
                <p>
                  Even if you’re a native English speaker, this might still be a
                  little hard to parse on the first go. The three uses of “or”
                  force you to hold a large number of qualifiers in your head
                  while processing it. This, combined with the inherently
                  abstract nature of what the definition is trying to describe,
                  results in something a bit difficult to digest.
                </p>
                <h3>Overcoming the Challenges</h3>
                <p>
                  Here are some simple techniques you can use to smoothly
                  overcome the challenges explained above and successfully make
                  the transition. In this section we’ll give an overview of some
                  techniques, and in a later section we’ll go into the
                  nitty-gritty details of how exactly to apply them.
                </p>
                <p>
                  First, let’s talk about learning the “dictionary vocabulary.”
                  Your first thought might be to acquire a list of all the
                  dictionary vocabulary used in your specific target language.
                  Now, if you’re able to find such a list, it may indeed be
                  useful.
                </p>
                <p>
                  But, there’s really no need to do that because
                  <b
                    >we can simply use the monolingual dictionary itself as a
                    list</b
                  >. All you have to do is start looking up words in the
                  monolingual dictionary, and if the definition contains any
                  words that you don’t know, then use the bilingual dictionary
                  to learn those words. In this way, we can use the bilingual
                  dictionary as a springboard to help us reach the point of
                  being able to function fully monolingually.
                </p>
                <p>
                  Once you reach Stage 2, it starts becoming important to
                  <b>choose wisely</b> which words you add to the SRS (we’ll
                  talk about this in the SRS section as well). Memorizing rare
                  words that won’t come up in your immersion is not only a waste
                  of time, but also the loss of an opportunity to learn
                  something actually useful. Despite that, in the context of
                  making the monolingual transition, we recommend assuming that
                  nearly every word you see used in a definition is worth
                  learning.
                </p>
                <p>
                  By definition, “dictionary vocabulary” is comprised of words
                  that aren’t very useful outside the context of the dictionary.
                  However, because being able to function monolingually brings
                  so many benefits, anything that improves your ability to
                  understand definitions is valuable and worth learning. So, in
                  general, once you begin transitioning, start to prioritize
                  learning words that you come across inside of monolingual
                  definitions, <b>even if they seem uncommon.</b>
                </p>
                <p>
                  When it comes to learning to understand the dense grammar and
                  unique writing style of monolingual definitions, patience and
                  effort are required. At first, you’re not going to understand
                  very much. Even so, if you are ready to make the transition,
                  there will be definitions here and there that you’ll be able
                  to understand.
                </p>
                <p>
                  Over time, as you read more and more definitions, your brain
                  will gradually figure out how to parse more and more
                  complicated definitions. Ultimately, like everything else in
                  language acquisition, things boil down to practice. It’s only
                  through <b>struggling </b>through it that you’ll come out on
                  the other end with meaningful ability.
                </p>
                <p>
                  That said, there is a simple technique that you can use to
                  expedite this process;
                  <b
                    >reading the monolingual definitions of words you already
                    know.</b
                  >
                  If you look up in the monolingual dictionary a target language
                  word that you already know, then you will know from beforehand
                  the general meaning that the definition will try to express
                  (we’ll talk more about this in Phase 1 of the monolingual
                  transition). This will make the definition much more
                  comprehensible, and can help illuminate how the grammar is
                  functioning.
                </p>
                <h3>When to Go Monolingual and How Long It Takes</h3>
                <p>
                  There is a lot of room for customization and personal
                  experimentation when it comes to making the monolingual
                  transition. The first thing to think about is when to start
                  the transition.
                </p>
                <p>
                  The longer you postpone going monolingual, the more ability
                  you will have at the time of transition, and the easier
                  transitioning will be. Conversely, the earlier you make the
                  transition, the more difficult it will be; but all the sooner
                  you’ll begin receiving the benefits. Some people are excited
                  by the idea of the going monolingual, feeling called to start
                  as early as Stage 1. Others are more wary of the work
                  required, and are tempted to perpetually put it off.
                </p>
                <p>
                  Our recommendation is to
                  <b>attempt transitioning as soon as you feel motivated to</b>.
                  There’s really no harm in starting “too early.” If you find
                  yourself feeling unready and overwhelmed when you start
                  working with the monolingual dictionary, consider trying again
                  in a month or two. There’s nothing wrong with that. Because
                  the monolingual dictionary is comprised of raw text, you’re
                  likely to have a smoother transition once you’ve gained a
                  basic foundation in reading raw text (how to do that is
                  discussed in the Raw Text section).
                </p>
                <p>
                  <b>Although</b>, if you find yourself nearing the end of Stage
                  2 without having made the transition, we recommend making it a
                  priority. Being able to function monolingually is one of the
                  criteria for completing Stage 2.
                </p>
                <h3>How Quickly to Complete the Transition</h3>
                <p>
                  The next thing to think about is how quickly you intend to
                  complete the transition.
                </p>
                <p>
                  One option is to <b>set aside your usual immersion</b> in
                  order to spend nearly all of your active study time working on
                  the transition. In this scenario, you might spend all of your
                  active immersion reading the monolingual dictionary, in
                  addition to learning only “dictionary vocabulary” inside of
                  Anki. If you took this approach, assuming that you already
                  have a foundation in reading raw text and at least a few hours
                  available daily to dedicate to transitioning, you may be able
                  to finish most of the transition within a few weeks.
                </p>
                <p>
                  On the other hand,
                  <b>it’s also possible to make the transition gradually</b>
                  over the course of six months (approx.), continuing general
                  immersion and studies alongside making the transition.
                </p>
                <p>
                  Either of these options is completely fine, as well as
                  everything in between. For example, you could make the first
                  half of the transition gradually, and then spend a week of
                  concentrated study to complete the transition. What you choose
                  largely boils down to personal preference.
                </p>
                <h2 id="phases-monolingual">
                  The Phases of the Monolingual Transition
                </h2>
                <p>
                  Here we are, at the nitty gritty of the monolingual
                  transition. There are four phases to the monolingual
                  transition, which are largely distinguished by the
                  <i>exercises </i>you do. As you progress through the
                  transition, you will use the monolingual dictionary
                  increasingly often. With that out of the way, let’s get
                  started with Phase 1.
                </p>
                <h3>Phase 1</h3>
                <p>
                  In order to perform the following exercise, start by setting
                  aside some time specifically for working with the monolingual
                  dictionary.
                </p>
                <p>
                  First,
                  <b
                    >take a target language word you already know and look it up
                    in the monolingual dictionary</b
                  >. Then, read the definition and see if you can understand it.
                </p>
                <p>
                  If there are no unknown words and you’re able to understand
                  it, then great! If there are no unknown words, but you’re not
                  quite able to understand it, that’s fine too. It’s bound to
                  happen and it should be expected.
                </p>
                <p>
                  Once you’re done working with a word in this way, simply
                  choose a new word to look up and repeat the process. That is,
                  <i>unless there are unknown words in the definition</i>.
                </p>
                <p>
                  Now,
                  <b
                    >in the case that there are unknown words in the
                    definition</b
                  >, look to see if any of the sentences inside the definition
                  are <b>1T</b> (one-target; containing exactly one unknown
                  word). If a 1T sentence exists, look its
                  <b>unknown word</b> up in the
                  <b><i>bilingual </i>dictionary</b>. Then, check to see if
                  doing so allows you to understand the sentence.
                </p>
                <ul>
                  <li>
                    If it does, then make a text-based sentence card with: the
                    1T sentence from the definition on the front, and the
                    bilingual definition of its unknown word on the back.
                  </li>
                  <li>
                    If you still don’t understand the sentence after looking up
                    the unknown word, then either: do nothing, or add the word
                    to a list of words to learn later.
                  </li>
                </ul>
                <p>
                  If the definition contains only sentences that are MT
                  (multi-target; contains multiple unknown words), then either:
                  do nothing, or add those words to a list of words that you
                  want to learn later.
                </p>
                <p>
                  Later, for each word on the list that you’ve been making, use
                  a search engine or example sentence website to find a 1T
                  sentence that contains that word, and make a text-based
                  sentence card with: that new 1T sentence on the front (that
                  you just searched for), and the bilingual definition of the
                  word on the back.
                </p>
                <p>
                  <b>Outside of doing this specific exercise</b>, you’ll want to
                  return to using the bilingual dictionary to look words up
                  while immersing and making cards.
                </p>
                <p>
                  This Phase 1 exercise will help you simultaneously learn
                  dictionary vocabulary and adjust to the grammar of monolingual
                  definitions. If you’re making a more gradual transition, you
                  could spend your first month doing the exercise for five
                  minutes a day. If you’re making a more condensed transition,
                  you could make this exercise the core of the first week or so
                  of your transition.
                </p>
                <p>
                  Once you’ve made around 100 sentence cards with this exercise,
                  move on to the next phase.
                </p>
                <h3>Phase 2</h3>
                <p>
                  For this exercise, prepare by gathering 1T sentences while
                  doing your general immersion. <b>Don’t</b> make Anki cards for
                  these 1T sentences on the spot. Instead, add them to a list of
                  sentences that you want to make cards for later.
                </p>
                <p>
                  Set aside some time specifically for working with the
                  monolingual dictionary. Take one of the 1T sentences you found
                  while immersing and look up the <b>target word</b> (the
                  unknown word) in the monolingual dictionary. Read the
                  definition and see if you can understand the sentence:
                </p>
                <ul>
                  <li>
                    If there are no unknown words and you’re able to understand
                    the sentence, then make a (monolingual) text-based sentence
                    card with: the 1T sentence on the front, and the monolingual
                    definition of the word on the back.
                  </li>
                  <li>
                    If there are unknown words in the monolingual definition,
                    then make (bilingual) sentence cards to learn the unknown
                    words. In the case that the monolingual definition doesn’t
                    contain an adequate 1T sentence for an unknown word, find
                    replacement 1T sentences for the unknown words using a
                    search engine or example sentence website.
                  </li>
                  <li>
                    In both of the above scenarios, make sure to also make
                    mental notes of what each of the unknown words mean.
                  </li>
                </ul>
                <p>
                  After making the bilingual cards for the unknown words and
                  taking mental notes of what they mean, see if the original
                  monolingual definitions become comprehensible. Then:
                </p>
                <ul>
                  <li>
                    If the monolingual definition still doesn’t make sense, then
                    simply don’t make a monolingual sentence card for the word.
                    If you still want to learn the word, you can make a
                    bilingual sentence card out of it.
                  </li>
                  <li>
                    If the monolingual definition now makes sense, then make a
                    monolingual sentence card with: the original 1T sentence for
                    the word on the front, and the monolingual definition of the
                    word on the back. In some cases, you may also want to add
                    the bilingual definitions to the back of the card.
                  </li>
                </ul>
                <p>
                  Now, don’t study the above monolingual sentence card
                  immediately. <b>Suspend it in Anki</b>. Wait until a couple
                  days have passed after having learned the bilingual cards for
                  the unknown words. Then <b>un-suspend</b> it and continue
                  learning it normally from there.
                </p>
                <p>
                  Outside of doing this specific exercise, continue using the
                  bilingual dictionary to look words up while immersing and
                  making cards.
                </p>
                <p>
                  This exercise gets you actually learning new words through
                  monolingual definitions, while helping you to continue
                  learning dictionary vocabulary.
                </p>
                <p>
                  Once you’re able to understand
                  <b>around 75% of monolingual definitions</b> without looking
                  anything up, move on to the next stage. By “understand,” we
                  mean understanding <i>enough </i>of the definition to get a
                  <b>solid grasp</b> on what the word being defined means. As
                  long as this is the case, it’s fine if there are portions of
                  the definition that you don’t understand.
                </p>
                <h3>Phase 3</h3>
                <p>
                  Continue the processes laid out in Phase 2, except
                  <i>now </i>when you come across an unknown word inside of a
                  monolingual definition you’ll be doing something different.
                </p>
                <p>
                  <b>Instead </b>of looking up the unknown word in the bilingual
                  dictionary, look that unknown word up in the
                  <b>monolingual dictionary</b>. This is what’s known as a “<b
                    >recursive look-up</b
                  >.” Multi-layered recursive look-ups can get messy and
                  frustrating very quickly; so, if you’re still at a loss for
                  understanding a word after going two layers deep, simply
                  revert to the bilingual dictionary.
                </p>
                <p>
                  In this phase, instead of setting aside time specifically for
                  practicing with the monolingual dictionary, simply start
                  making <i>all </i>of your cards this way. This means that most
                  of the cards you make shouldn’t contain any English<b>.</b>
                </p>
                <p>
                  Outside of making cards, you can continue to use the bilingual
                  dictionary for quickly looking things up while immersing.
                </p>
                <p>
                  Move onto Phase 4 once you feel comfortable<b> </b>enough with
                  the monolingual dictionary to begin using it for on-the-fly
                  look-ups as well, in addition to card creation.
                </p>
                <h3>Phase 4</h3>
                <p>
                  Continue the process laid out in Phase 3, except start
                  transitioning to using the monolingual dictionary
                  <i>outside of making cards as well</i>. Start moving toward
                  reaching the point where, even when doing quick look-ups while
                  immersing, you virtually always use a monolingual dictionary.
                </p>
                <p>
                  When you first start this phase, it’s fine to use the
                  monolingual dictionary for quick look-ups when you have
                  energy; but, reverting back to the bilingual dictionary when
                  tired. Similarly, if you look something up in the monolingual
                  dictionary and it doesn’t immediately click, it’s fine to
                  instantly revert back to the bilingual dictionary rather than
                  grappling with the monolingual definition and doing recursive
                  look-ups (this applies to
                  <i>look-ups while immersing only</i>). Over time,
                  <b
                    >as your comfort with the monolingual dictionary continues
                    to increase, gradually reduce how often you revert to the
                    bilingual dictionary.</b
                  >
                </p>
                <p>
                  Once you rely solely on the monolingual dictionary for over
                  95% of all your look-ups, when making cards as well as looking
                  things up on the fly, you’ll be functioning fully
                  monolingually. Now, your monolingual transition is considered
                  complete.
                </p>
                <h2>Weaknesses of the Monolingual Dictionary</h2>
                <p>
                  Certain categories of words have
                  <b>disproportionately </b>complex and convoluted monolingual
                  definitions. While in the process of making the transition,
                  it’s a good idea to simply revert to the bilingual dictionary
                  for these kinds of words. For some specific categories of
                  words, you will want to continue relying on the bilingual
                  dictionary even after fully completing the monolingual
                  transition.
                </p>
                <p>
                  First, we have words with excessively simple meanings. It may
                  seem counterintuitive at first, but when a word refers to
                  something extremely simple, sometimes it actually becomes
                  quite complicated to define.
                </p>
                <p>
                  For example, take
                  <a
                    href="https://www.merriam-webster.com/dictionary/thing"
                    target="_blank"
                    rel="noopener"
                    >the following Merriam-Webster definition:</a
                  >
                </p>
                <blockquote class="bq">
                  <p>
                    “an object or entity not precisely designated or capable of
                    being designated.”
                  </p>
                </blockquote>
                <p>This is the first definition listed for the word “thing.”</p>
                <p>Another example is:</p>
                <blockquote class="bq">
                  <p>
                    “used as a function word to indicate that a following noun
                    or noun equivalent is definite or has been previously
                    specified by context or by circumstance.”
                  </p>
                </blockquote>
                <p>
                  That’s
                  <a
                    href="https://www.merriam-webster.com/dictionary/the"
                    target="_blank"
                    rel="noopener"
                    >Merriam-Webster’s first definition of “the.”</a
                  >
                </p>
                <p>
                  To make things worse, these sorts of words often have a
                  multitude of slightly different meanings. Specifically,
                  Merriam-Webster lists over 20 different meanings of “the.”
                  These technical explanations of simple words can be
                  interesting to read once you’ve mastered the monolingual
                  dictionary, but they aren’t good training material. Before you
                  finish the transition, it’s best to simply consult the
                  bilingual dictionary for these kind of words.
                </p>
                <p>
                  Next, we have nouns. Many nouns simply don’t lend themselves
                  to being described with words very well. For example, the
                  <a
                    href="https://www.merriam-webster.com/dictionary/piano"
                    target="_blank"
                    rel="noopener"
                    >Merriam-Webster definition of “piano”</a
                  >
                  is:
                </p>
                <blockquote class="bq">
                  <p>
                    “a musical instrument having steel wire strings that sound
                    when struck by felt-covered hammers operated from a
                    keyboard.”
                  </p>
                </blockquote>
                <p>
                  If you had never seen a piano before, that description is
                  unlikely to give you a good sense of what a piano actually
                  looks like and how it works.
                </p>
                <p>
                  Another similar issue is that, in order to be accurate,
                  sometimes a monolingual dictionary will describe everyday
                  items in <b>technical terms</b>; which has the potential to be
                  confusing even to native speakers. For example, the
                  <a
                    href="https://www.merriam-webster.com/dictionary/camera"
                    target="_blank"
                    rel="noopener"
                    >Merriam-Webster definition of “camera”</a
                  >
                  is:
                </p>
                <blockquote class="bq">
                  <p>
                    “a device that consists of a lightproof chamber with an
                    aperture fitted with a lens and a shutter through which the
                    image of an object is projected onto a surface for recording
                    (as on a photosensitive film or an electronic sensor) or for
                    translation into electrical impulses (as for television
                    broadcast).”
                  </p>
                </blockquote>
                <p>
                  If you look up a noun in the monolingual dictionary and figure
                  that the definition is going to be either too technical or
                  abstract to be helpful, it’s probably best to just let it go.
                </p>
                <p>
                  <i>But</i>, before reverting to the bilingual dictionary,
                  consider searching the word in Google Images. When it comes to
                  nouns,
                  <b>often times a picture really is worth a thousand words</b>.
                  If you find a picture that clears up the meaning of a word,
                  feel free to put it on the back of an Anki card instead of a
                  monolingual definition. We consider images to be
                  <i>completely fair game</i> as far as functioning
                  monolingually goes. If the word in question is a place name,
                  color, species of plant or animal, or really any kind of
                  physical object, Google Images is likely to be more helpful
                  than the monolingual dictionary.
                </p>
                <p>
                  It’s also important to note that the monolingual definitions
                  of everyday nouns often contain words which fall
                  <b>outside </b>of what we would call “dictionary vocabulary.”
                </p>
                <p>
                  We defined “dictionary vocabulary” as words that are generally
                  uncommon, but show up frequently within monolingual
                  definitions. Within the monolingual definitions of these
                  everyday nouns, oftentimes words are used that are
                  <i>uncommon both inside and outside of the dictionary</i>.
                </p>
                <p>
                  For example, take the definition of “camera” from before. It
                  uses words such as “aperture” and “photosensitive,” which are
                  <b>highly domain specific</b> and unlikely to show up in daily
                  life and within other definitions. So, be sure to watch out
                  for this, as you probably want to
                  <b
                    >avoid spending time learning domain specific words
                    initially.</b
                  >
                </p>
                <p>
                  Finally, there is one last subcategory of nouns that (even
                  after mastering the monolingual dictionary) you will probably
                  want to use the bilingual dictionary to learn:
                  <b>technical academic terminology.</b>
                </p>
                <p>
                  These include words like the names of historic events, the
                  names of planets, and scientific terms such as “atom” or
                  “neuron.” Because these words have standardized technical
                  meanings within their respective domains,
                  <b>they have exact one-to-one correlates in every language</b
                  >. The word “hydrogen” means the exact same thing as the
                  Japanese and French equivalents for “hydrogen.” One way to
                  think about it is that each of these words point to a specific
                  concept, and the concept itself is
                  <b>language independent</b>.
                </p>
                <p>
                  So, if you already know what “hydrogen” means in English, then
                  there’s no need to learn your target language’s word for
                  “hydrogen” through the monolingual dictionary. Simply
                  associating the English word “hydrogen” with your target
                  language’s equivalent is enough. In fact, even though the
                  opposite is true for most words, making clear associations in
                  your head between native language and target language
                  technical terms is useful, as it will help you easily transfer
                  technical knowledge you gain in one language into the other.
                </p>
                <h2>Wrapping up the Monolingual Transition</h2>
                <h3>Language Distance and Going Monolingual</h3>
                <p>
                  If your target language is closely related to your native
                  language, going monolingual won’t be quite as important, and
                  likely won’t be as beneficial.
                </p>
                <p>
                  Like we mentioned in the previous section, all languages share
                  technical academic terms. When talking about completely
                  unrelated languages like English and Japanese, those are just
                  about the only true one-to-one correlates you’re going to find
                  between the two languages. With these languages, a monolingual
                  definition is usually going to trump a bilingual one in terms
                  of accuracy.
                </p>
                <p>
                  But, if two languages are genealogically related, that means
                  that at
                  <b
                    >one point in history they were literally the same
                    language</b
                  >. Because of this, related languages often still share many
                  <b>one-to-one cognates</b> outside of technical terminology as
                  well. When learning these words, the bilingual dictionary is
                  most useful.
                </p>
                <p>
                  So, if you’re learning a language that’s closely related to
                  your native language, we recommend putting<b>
                    less overall emphasis</b
                  >
                  on the monolingual transition. This can take the form of
                  waiting longer to begin the transition, and continuing to
                  reference bilingual dictionaries alongside monolingual ones
                  even after making the transition.
                </p>
                <h3>Misc. Tips about Dictionaries &amp; Going Monolingual</h3>
                <p>
                  Paper dictionaries can be fun to flip through, but for our
                  purposes it’s crucial that you have access to electronic
                  dictionaries. Being able to instantly look up words and
                  copy/paste their definitions onto cards is a game changer in
                  the world of language learning.
                </p>
                <p>
                  If you can, try to find as many different monolingual
                  dictionaries as you can for your target language.
                  <b>Every dictionary will have its own flavor</b>, pros, and
                  cons. Some are more technical, some use more everyday
                  language. Some are more comprehensive, some more bare bones.
                </p>
                <p>
                  Because every dictionary is different, the more dictionaries
                  you have at your disposal, the higher the chance that you’ll
                  be able to understand at least one of their definitions for a
                  given word. Additionally, comparing and contrasting
                  definitions from different dictionaries can help reveal the
                  true meaning of the word in question. It’s also fine to put
                  multiple definitions of the same word on a single Anki card if
                  you think it will help you grasp the meaning.
                </p>
                <p>
                  We also recommend finding at least one
                  <b>monolingual dictionary app</b> for your phone. This way,
                  you will be able to look things up monolingually wherever you
                  are. This will become particularly useful once you reach Phase
                  4 of the Monolingual Transition, where you will begin using
                  the monolingual dictionary for on-the-fly look-ups as well.
                </p>
                <p>
                  Lastly, if a monolingual definition really isn’t clicking,
                  another option you can try before resorting to the bilingual
                  dictionary is <b>googling the word or phrase in question</b>,
                  along with the word for “meaning” in your target language. If
                  you’re lucky, you’ll be able to find a forum or blog post
                  where someone has given the meaning of the word or phrase in
                  simple language. Feel free to copy and paste these sorts of
                  casual descriptions onto your cards as well.
                </p>
                <h3>Monolingual Transition Q&amp;A</h3>
                <h4>
                  Even when I understand monolingual definitions, things are
                  blurry. Wouldn’t I understand more with the bilingual
                  dictionary?
                </h4>
                <p>
                  Everyone feels like this at first. Just like with learning to
                  read raw text, things start out blurry and gradually get
                  clearer over time. This is simply the nature of the learning
                  process. It may be true that, initially, using the monolingual
                  dictionary will cause a drop in your level of understanding.
                  But,
                  <b
                    >persevering through the initial period will be well worth
                    it in the long term</b
                  >. Once you master the monolingual dictionary, you’ll be able
                  to understand words incomparably greater than you were ever
                  able to with the bilingual dictionary.
                </p>
                <h4>
                  I understood part of a monolingual definition, but not the
                  whole thing. Can I still use this definition to learn the
                  target word?
                </h4>
                <p>
                  Yes. Even if you didn’t understand an entire monolingual
                  definition, as long as you understood <i>enough </i>to get
                  what the word you looked up means, then that is enough.
                </p>
                <p>
                  With that said, if the definition in question contained any
                  unknown words, it may be a good idea to add them to a list to
                  learn later. Remember, in general, words that show up within
                  monolingual definitions are worth learning.
                </p>
                <h4>
                  Sometimes I think I understood a monolingual definition, but
                  I’m not completely sure. Is it okay to double check with the
                  bilingual dictionary?
                </h4>
                <p>
                  Yes, it’s perfectly fine to double check with the bilingual
                  dictionary. However, don’t put the bilingual definition on
                  your Anki card. Just confirm that your interpretation of the
                  monolingual definition was correct, and then go with that.
                  Some people worry that looking at a bilingual definition even
                  once will “spoil” their understanding of the word. But, this
                  isn’t something you need to worry about. If you can understand
                  the monolingual definition, looking at an English translation
                  won’t cause any harm.
                </p>
                <h4>
                  If a word has multiple different meanings, should I put the
                  entire definition on the back of my card, or only the portion
                  necessary for understanding the target sentence?
                </h4>
                <p>
                  It’s fine either way, but our recommendation is to put the
                  entire definition, and perhaps bolding the portion relevant to
                  the target sentence. This way, when reviewing the card, even
                  if you only read the portion that’s bolded, you’ll still see
                  the rest of the definition out of the corner of your eye; this
                  will help you remember that the word has multiple meanings.
                  Due to that, when you come across the word in the wild,
                  <b
                    >you will be quicker to recognize when the word is being
                    used with a meaning other than the one you memorized.</b
                  >
                </p>
                <h4>
                  Can I make sentence cards out of the contents of monolingual
                  definitions?
                </h4>
                <p>
                  Totally. Any piece of language that was created by a native
                  speaker for other native speakers is game when it comes to
                  sentence mining, including monolingual definitions. If you
                  want to learn a word that you found in a monolingual
                  definition, but the definition lacks context or is too
                  abstract, it’s also fine to search for a replacement sentence
                  for that word.
                </p>
                <h4>
                  After going monolingual, should I go back and remove the
                  bilingual definitions from all my old Anki cards?
                </h4>
                <p>
                  Definitely not. That would take far more time and effort than
                  it would be worth. As you continue to review in Anki, you will
                  see your old Anki cards progressively less and less often
                  anyway.
                </p>
                <h4>
                  When reviewing monolingual sentence cards, should I memorize
                  monolingual definitions verbatim?
                </h4>
                <p>
                  That would be far too difficult; don’t do that. Just like with
                  bilingual sentence cards, simply try to remember the basic
                  gist of the word’s definition.
                </p>
                <h1 id="srs">SRS</h1>
                <p>
                  Besides shifting away from bilingual sentence cards with the
                  monolingual transition, not much changes between Stage 1 and
                  Stage 2 on the front of sentence mining and the SRS.
                </p>
                <p>
                  At this point, you may find it useful to go back and reread
                  the <b>“Sentence Cards”</b> and
                  <b>“Sentence Mining” </b>portions of the Stage 1 guide
                  (currently the
                  <a
                    href="https://massimmersionapproach.com/table-of-contents/stage-1/jp-quickstart-guide/#sentence-mining"
                    target="_blank"
                    rel="noopener"
                    >“Japanese Quickstart Guide”</a
                  >). Now that you have more experience under your belt, some of
                  the advice may take on new meanings for you.
                </p>
                <p>
                  If you maintain the recommended immersion to SRS time
                  allocation ratio (covered in the
                  <b>“Sentence Mining Q&amp;A” </b>section of the
                  <b>Japanese Quickstart Guide</b>), you should be ready to move
                  on from Stage 2 to Stage 3 after making another
                  <b>~3,000 sentence cards</b>. Combined with the ~3,000 you
                  made in Stage 1, this should take you to
                  <b>~6,000 total sentence cards</b>.
                </p>
                <p>
                  One element of how you mine sentences that you may want to
                  change moving into Stage 2 is how you choose what sentences
                  are worth mining.
                </p>
                <h3>Choosing What Sentences Are Worth Mining</h3>
                <p>
                  In Stage 1, we recommended choosing what sentences to mine
                  based primarily on what grabs your attention. If a word seems
                  to stand out or feel familiar, that’s likely a sign that you
                  have already come across that word many times before. And, if
                  the same word is showing up multiple times in your immersion,
                  that means that it’s at least relatively common and definitely
                  worth your time to learn.
                </p>
                <p>
                  But,
                  <b
                    >by the time you reach Stage 2, you will have already
                    learned most extremely common words in the language</b
                  >. Once this happens, it might become rare that you come
                  across unknown words that feel familiar or pull your attention
                  in the same way that words did in Stage 1. Once this happens,
                  it can become hard to judge how valuable a word will be before
                  learning it. Once you reach this point,
                  <b>frequency lists</b> can become a useful tool.
                </p>
                <h3>
                  Making Use of Frequency Lists to Determine the Words You Learn
                </h3>
                <p>
                  The traditional way to use a frequency list is to start at the
                  top of the list, and learn every word listed in order. As
                  mentioned in Stage 1, learning the most common 1,000 words in
                  this way can be an effective way to jump-start learning a new
                  language.
                </p>
                <p>
                  But, after that, choosing what words to learn based solely on
                  a frequency list isn’t an efficient strategy. The reason for
                  this is that <b>after the first~1,000 words on the list</b>,
                  the frequency of specific words starts
                  <b>differing greatly</b> depending on the specific sources the
                  list was created from. So, even if a frequency list you found
                  online says a word is common, in reality it might not come up
                  often in your own personal immersion.
                </p>
                <p>
                  In Stage 2 and above, we recommend using frequency lists to
                  help you roughly judge the value of words you come across in
                  your immersion. For example, if you searched the target word
                  of a 1T sentence up in a frequency list and it was positioned
                  around 4,000 on the list, then you could assume it’s worth
                  learning. Conversely, if it was positioned around 40,000 on
                  the list, you could assume it’s extremely uncommon and not
                  worth your time to learn. So, you’re not going to a frequency
                  list to find new words to learn, but simply using it to gauge
                  the usefulness of <b>words</b>
                  <b>that showed up in your immersion.</b>
                </p>
                <p>
                  For the duration of Stage 2, we recommend focusing on learning
                  words positioned below 15,000 on a frequency list.
                  <b
                    >As you continue to rise up the stages, you will continue to
                    expand the frequency range of words that you learn</b
                  >. Of course, the exception to this principle is dictionary
                  vocabulary, which, for reasons explained in the Monolingual
                  Transition section, you should generally value regardless of
                  frequency list position.
                </p>
                <p>
                  Always keep in mind that the frequency list you use likely
                  won’t be completely representative of your own personal
                  immersion. If a frequency list says a word is uncommon but
                  your intuition tells you to learn it, feel free to go ahead
                  and learn it anyway.
                </p>
                <p>
                  If you’re not able to find a frequency list for your target
                  language, or you simply prefer to not constantly reference a
                  frequency list, it’s also fine to continue relying solely on
                  your intuition when picking what words to learn from your
                  immersion. Any words that are truly important will be learned
                  sooner or later, and learning an uncommon word here and there
                  won’t do any harm.
                </p>
              </div>
