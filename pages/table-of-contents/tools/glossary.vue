    <h2 id="1T">One-Target Sentence</h2>
    <strong>Also known as:</strong> 1T sentence, single-target sentence. A
    sentence that contains one unknown word or grammar structure. The unknown
    word or structure is referred to as the "target word" or "target structure".
    A sentence is only considered to be 1T if, after looking up or inferring the
    meaning of the unknown word/structure, the overall meaning of the sentence
    becomes clear. If a sentence contains one unknown word/structure, but the
    overall meaning remains unclear even after looking up the target
    word/structure, the sentence is not considered to be 1T. Learning through 1T
    sentences can be thought of as "picking low-hanging fruit". It makes the
    target word/structure easy to understand and retain. As you continue to
    learn, sentences that were previously one-target will become zero-target,
    and sentences that were previously multi-target will become one-target. In
    this way, one-target sentences can take you all the way to fluency.

    <strong>Note</strong>: Previously, MIA referred to "1T sentences" as "i+1
    sentences". The term "i+1" comes from Stephen Krashen and his Input
    Hypothesis. From
    <nuxt-link
      to="https://en.wikipedia.org/wiki/Input_hypothesis"
      rel="noopener"
      >Wikipedia</nuxt-link
    >: "[The Input Hypothesis] states that learners progress in their knowledge
    of the language when they comprehend language input that is slightly more
    advanced than their current level. Krashen called this level of input "i+1",
    where "i" is the learner's interlanguage and "+1" is the next stage of
    language acquisition." In other words, Krashen's "i+1" refers to input that
    is immediately comprehensible to the unconscious language acquisition
    device. On the other hand, MIA was using the term to refer to input that
    could be consciously comprehended after looking up or inferring the meaning
    of an unknown word or structure. In order to avoid confusion due to this
    slight difference in meaning, we decided to drop the term and adopt the more
    straightforward "one target" instead.

