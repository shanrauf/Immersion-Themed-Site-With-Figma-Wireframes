    <h2>Accent</h2>
    The manner in which one pronounces a language. Can be broken down into
    <em>pronunciation of vowels and consonants</em>,
    <em>stress accent/ pitch accent/ tones</em> (depending on the language in
    question), and <em>intonation</em>. As an L2 speaker, accent is arguably the
    most difficult aspect of language to fully master. The term “native accent”
    is vague, as native speakers of a language will pronounce the language
    differently depending on their generation, social class, specific area, and
    personality. That said, talking to an unsuspecting native speaker on the
    phone and seeing if they notice that you are an L2 speaker or not is a
    practical way to test how native-like your accent is. It is extremely rare
    for adult learners to achieve the level of accent authenticity required to
    pass this test, but people such as
    <nuxt-link to="https://en.wikipedia.org/wiki/Dashan" rel="noopener"
      >Dashan</nuxt-link
    >
    and
    <nuxt-link to="https://www.youtube.com/watch?v=PYlnJpvRwX8" rel="noopener"
      >Julien Gaudfroy</nuxt-link
    >
    have proved that it is indeed possible. Developing a native-like accent
    hinges on the ability to accurately perceive and notice the subtleties of
    spoken language. Many factors go into the development of accent, but
    <em>waiting to output</em> and
    <em>prioritizing listening over reading</em> in the early stages seem to
    consistently lead to developing a more native-like accent. Age and
    motivation also seem to play a large role. Consciously studying the phonetic
    system of the target language may allow one to become able to perceive and
    notice greater levels of subtlety when getting input, leading to acquiring a
    more native-like accent. Techniques such as <em>adopting a parent</em> and
    <em>shadowing</em> can also help one obtain a more native-like accent, but
    require a basic level of proficiency in the language to be effective.

