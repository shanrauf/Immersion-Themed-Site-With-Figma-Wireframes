    <h2>AJATT</h2>
    AJATT, short for “All Japanese All the Time,” is a language learning blog
    created by Khatzumoto in 2006. The goal of AJATT was to document and improve
    upon the methods Khatzumoto used to become fluent in Japanese in the short
    span of 18 months. The method that he described (later known as “The AJATT
    Method”) was very similar to the <em>Antimoon</em> method, with the addition
    of James Heisig’s <em>Remembering the Kanji</em> and an emphasis on mindset
    and the psychology of success. Khatzumoto stressed the importance of being
    self-directed, creative, and willing to experiment. He argued that
    extraordinary results can’t be achieved with ordinary action, and encouraged
    people not to be held back by the desire for social acceptance. Above all,
    the bedrock of AJATT was the assertion that enjoying the process is an
    absolutely essential ingredient for success. Unfortunately, this greater
    message was lost on many, which resulted in AJATT becoming polarizing and
    controversial. Nevertheless, AJATT had a large impact on late 00’s Japanese
    learning community, popularizing the SRS and <em>Remembering the Kanji</em>,
    and spreading awareness of Stephen Krashen’s <em>Input Hypothesis</em>.
    Although AJATT began as a free blog, around 2010, Khatzumoto shifted his
    focus to creating questionable language learning products, which he sold
    with sleazy marketing techniques at unreasonably high prices. This
    aggressive marketing led many to mistakenly assume that all of AJATT,
    including the original free content, was nothing more than a scam, severely
    damaging AJATT’s reputation. Khatzumoto became increasingly less active, and
    eventually completely disappeared from the internet in 2014. He resurfaced
    in 2017 with the announcement that he had created a
    <nuxt-link to="https://www.patreon.com/ajatt" rel="noopener"
      >Patreon</nuxt-link
    >, and will be writing articles on self-help and language learning for to
    those who subscribe. These new articles don’t seem to have attracted very
    much attention. MIA is heavily inspired by AJATT. Although there are areas
    in which they disagree, MIA and AJATT are much more alike than different.
    MIA’s stance is that AJATT’s biggest weakness is not its <em>ideas</em>, but
    the way it <em>communicates</em> those ideas. One could say that,
    fundamentally, MIA was founded with the goal of bringing Khatzumoto’s
    insights to a larger audience.

