    <h2><nuxt-link id="active_study"></nuxt-link>Active Study</h2>
    <em>Active Study</em> refers to conscious, directed effort made to increase
    one’s knowledge of the target language. Active Study corresponds to
    <em>learning</em> in the <em>learning</em>/<nuxt-link to="#acquisition"
      ><em>acquisition</em></nuxt-link
    >
    dichotomy, and in the context of MIA, is usually contrasted with
    <em>Immersion</em>. Examples of Active Study include reading a grammar
    guide, watching a pronunciation tutorial on YouTube, or looking words up in
    a dictionary and creating Sentence Cards. In MIA, Active Study mostly
    revolves around making and reviewing SRS cards, although this is not always
    the case. Mastering a language without Active Study is possible, and there
    are examples of people successfully acquiring foreign languages through
    Immersion alone. That said, MIA's stance is that Active Study,
    <strong>when properly combined with Immersion</strong>, greatly accelerates
    the acquisition process. It is also worth noting that the value of Active
    Study decreases as one becomes more advanced in the language.

