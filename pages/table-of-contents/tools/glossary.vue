    <h2>
      <nuxt-link id="acquisition"></nuxt-link>Acquisition (as opposed to
      <em>Learning</em>)
    </h2>
    The distinction between <em>acquisition</em> and <em>learning</em> was
    proposed by Stephen Krashen in his <em>acquisition–learning hypothesis</em>,
    one of the five hypotheses that constitute the <em>input hypothesis</em>.
    The <em>acquisition–learning hypothesis</em> claims that people can develop
    linguistic skills in two distinctly different ways: <em>acquisition</em>,
    and <em>learning</em>.

    <em>Acquisition</em> is the natural, automatic and effortless process
    through which children become proficient in their native language. Krashen
    claimed that this ability to pick-up language naturally is not lost after
    childhood, and given the right conditions (sufficient quality and quantity
    of linguistic input), adults will acquire language as well. Acquisition
    occurs subconsciously through comprehending linguistic input that contains
    new vocabulary or grammatical structures. Because the process is
    subconscious, one is not aware when it is happening, and is unlikely to
    recognize that anything has changed immediately after the fact. Acquisition
    is most likely to occur when one is meaningfully engaged in the message
    (rather than the form) of linguistic input, e.g., when engrossed in a TV
    show or deep in a thought-provoking conversation. While “you” are busy
    following the plot or formulating arguments, a deeper part of the mind is
    working hard to decode the nuances of language. Subjectively, acquisition
    manifests itself in the form of linguistic intuitions you can’t logically
    explain, or realizing that you know the meaning of words you never learned.
    For example, if you are a native speaker of English, you likely find “the
    big red dog” much more natural than “the red big dog.” There is a
    <nuxt-link
      to="https://dictionary.cambridge.org/ja/grammar/british-grammar/about-adjectives-and-adverbs/adjectives-order"
      rel="noopener"
      >complicated grammatical rule</nuxt-link
    >
    dictating why this is the case, but unless you have formally studied English
    grammar, you most likely have no idea what it is. Notice how you didn’t have
    to "think" about anything to know which of the two phrases is most natural;
    you <em>just knew</em>. In this way, accessing what one has acquired is, in
    most cases, instantaneous, effortless and direct.

