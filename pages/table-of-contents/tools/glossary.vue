    <h2>Active Immersion</h2>
    <em>Active Immersion</em> is one of the three main categories of Immersion,
    along with <em>Passive Immersion</em> and <em>Background Immersion</em>. Any
    time that one’s <strong>full and undivided attention</strong> is being
    directed towards an activity that is directly mediated by the target
    language, one is getting Active Immersion. Some examples include sitting
    down and reading a book, watching a movie or TV show without multitasking,
    or having a conversation with a native speaker. Whether one is simply
    getting lost in the <em>content</em> of input (e.g., enjoying the plot of a
    novel like a native speaker would) or heavily focusing on the
    <em>form</em> of input (e.g., analyzing intonation patterns while listening
    to an audiobook), as long as one is fully engaged in a
    linguistically-mediated activity, one is actively immersing. Fundamentally,
    all true linguistic ability is cultivated through immersion; knowledge
    gained through
    <nuxt-link to="#active_study"><em>Active Study</em></nuxt-link> can improve
    the efficiency at which language is acquired through immersion, but can’t
    directly cause acquisition on its own. Passive and Background Immersion can
    be extremely beneficial, but tend to only bring results to the extent that
    one is regularly getting significant doses of Active Immersion. This is why
    Active Immersion is the linchpin of MIA and language acquisition as a whole.
    Without Active Immersion, one will get nowhere; if one did
    <em>only</em> Active Immersion and nothing else, they would eventually
    achieve fluency in the language.

