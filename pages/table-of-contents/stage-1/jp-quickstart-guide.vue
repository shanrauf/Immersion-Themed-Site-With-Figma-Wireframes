      <h2 id="choosing-content">Choosing Immersion Content</h2>
      <p>
        You can really use anything for your immersion content, as long as it
        was
        <strong>made by Japanese people, for Japanese people</strong>. It’s best
        to avoid content that has been artificially dumbed-down for learners;
        you need to train your brain to parse real, native speech. You learned
        your first language this way, and you can learn Japanese this way as
        well.
      </p>
      <p>
        Some other things to take into account are enjoyment and
        comprehensibility. You’re going to be doing a lot of immersion, so it’s
        crucial that you enjoy it. Anything boring has to go. And in general,
        you can learn more easily when you have some idea of what’s going on.
        That being said, in the beginning, try not to get too hung up on
        understanding stuff. When you first start out, pretty much nothing is
        going to be comprehensible, so we recommend mainly focusing on finding
        content that’s engaging.
      </p>
      <p>
        Some other guidelines for how to choose immersion content are:
      </p>
      <ul>
        <li>
          In the beginning, content that has a visual component, such as YouTube
          videos, dramas, and movies, are much easier to learn from. If you try
          listening to a podcast or audiobook as a beginner, you’ll likely have
          no idea what’s going on.
        </li>
        <li>
          Content with an ongoing narrative, such as anime or dramas, can be
          easier to learn from. Once you get familiar with the characters and
          settings, following the plot will become much easier, even if you’re
          not understanding much.
        </li>
        <li>
          Try to find content that you’re interested in. If you like anime,
          watch anime. If you like baseball, watch Japanese baseball. Also watch
          anime, J-drama, J-movies, and Japanese YouTube videos
          <em>about</em> baseball.
        </li>
        <li>
          Re-watch Japanese content you’ve watched with English subtitles in the
          past. This way, you’ll know what’s going on even if you’re not
          understanding much.
        </li>
        <li>
          Read English summaries of content you plan on consuming ahead of time
          in order to increase comprehensibility. If you’re watching a movie,
          try reading a summary of the plot on Wikipedia. If you’re watching an
          anime or drama, look for short episode summaries on Wikipedia. If
          you’re watching something on Netflix, there are short summaries
          directly pinned to each episode.
        </li>
      </ul>
      <h2 id="subtitles">Should I Use Subtitles?</h2>
      <p>
        In general, if you’re reading subtitles, you’re not actively listening.
        This is true for both English subtitles and Japanese subtitles. Actively
        watching Japanese content with English subtitles is equivalent to
        passive listening at best.
      </p>
      <p>
        With that said, if you’re completely new to the Japanese language and
        have zero background in consuming Japanese media, it may be a good idea
        to start out with English subtitles, at least some of the time. The most
        important thing is that you don’t quit. So, if jumping straight into raw
        Japanese content is too intimidating, then, by all means, start with
        English subs. But, see them as something to quickly outgrow, and try to
        slowly wean yourself off of them. Although it may seem counter-intuitive
        at first, you’ll actually learn much more from “understanding close to
        nothing while watching raw” than you will from “seemingly understanding
        everything while watching with English subs”.
      </p>
      <p>
        Then we have Japanese subtitles. In our view, watching with Japanese
        subtitles is closer to reading than listening. This is because true
        listening involves deducing what words are being spoken from audio
        alone. So, in a way, watching with Japanese subs is like solving a math
        problem by copying the answer from the back of the textbook. For this
        reason, in general, MIA considers “watching with Japanese subtitles” to
        be a form of reading.
      </p>
      <p>
        At the same time, Japanese subtitles are perhaps the best way to get
        familiar with written Japanese. Right from the start, they can help you
        consolidate kana and kanji, and allow you to notice new patterns in the
        written language. For this reason, we recommend starting to use Japanese
        subs as soon as you learn kana. That said,<strong>
          aim to spend at least half of your active immersion time not using any
          subtitles at all</strong
        >. That active listening practice is important.
      </p>
      <h2 id="looking-things-up">Should I Look Things Up?</h2>
      <p>
        Yes,<strong> if you feel like it</strong>. If a word pops out at you
        while listening to Japanese, oftentimes, looking it up once will be
        enough to permanently remember it. So, if you think you heard a word
        clearly enough to look it up and find it in a dictionary, consider
        giving it a go. But, never feel obligated to look things up. Attempting
        to look up too many words will put you on the fast track to burnout.
        Successful language learning requires consistency in the long term, so
        immersing in a way that’s sustainable should always be the top priority.
      </p>
      <p>
        To be specific, while immersing, we suggest aiming to look up a word
        <strong>once every few minutes</strong>. Unless, that is, you simply
        don’t feel like looking anything up at all. In that case, it’s perfectly
        fine to just sit back, relax and enjoy your immersion.
      </p>
