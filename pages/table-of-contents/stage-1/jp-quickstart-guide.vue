        cards. The more new cards you learn a day, the more daily reviews you’re
        going to have. The thing is, there’s a time delay between when you start
        learning more new cards and when your reviews start to skyrocket. If you
        try something crazy like learning 100 new cards a day, things might seem
        fine for a few days. But within a week or two, you’ll end up with
        hundreds upon hundreds of reviews a day. You can use this
        <a href="https://pheartheceal.github.io/anki-optimizer/" target="_blank"
