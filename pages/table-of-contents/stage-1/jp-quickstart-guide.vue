      <h2>A Three-Part Approach to Kanji</h2>
      <p>
        Just because you can recognize something, that doesn’t necessarily mean
        you can recall it from memory. For example, in English, sometimes you’re
        able to read a word, but not spell it. As you can see with this example,
        in general,
        <em>recognition</em> is much easier than
        <em>production<em>. </em></em>
      </p>
      <p>
        This is why, initially, we think it’s best to only worry about learning
        to
        <em>recognize</em> kanji characters. Using the right system and tools,
        this can be done easily and fairly quickly. And once you can recognize
        kanji, you can learn to read written Japanese without too much trouble.
      </p>
      <p>
        Learning to read Japanese will naturally lead you to become extremely
        familiar with kanji. That being said, since reading only requires
        character <em>recognition</em> (and not production), learning to read
        won’t automatically enable you to recall characters from memory. But, it
        will take you most of the way there.
      </p>
      <p>
        Let’s go back to spelling English for a moment. Think about those times
        when you can’t remember the spelling of a familiar word. Usually, you
        can see it in your mind’s eye, but just can’t quite recall it. Once you
        write out a few possibilities, you can intuitively tell which spelling
        is correct. This is how learning to produce kanji from memory will feel
        like after becoming able to read Japanese. And once you reach this
        point, with just a little practice, cultivating the ability to recall
        characters from memory will come very quickly.
      </p>
      <p>
        On the other hand, trying to learn to recall characters from memory
        before you actually know Japanese is extremely difficult. It can be
        done, but it’s almost certainly not worth the time and effort. This is
        why we recommend a three-part approach to learning kanji: 1) learn to
        <em>recognize</em> kanji, 2) learn to read Japanese, and 3) learn to
        <em>produce</em> kanji. This way, you can reach a point of kanji mastery
        without any serious pain or struggle.
      </p>
      <h2>Kanji Production is Overrated</h2>
      <p>
        When we say “kanji production<em>“</em> or “the ability to recall kanji
        from memory”, we’re referring to the ability to write out kanji
        characters by hand. This isn’t needed to be able to
        <em>type</em> Japanese because Japanese typing is done phonetically. So,
        as long as you can read kanji, you can type kanji as well.
      </p>
      <p>
        The ability to produce kanji from memory isn’t nearly as important as it
        used to be, since nowadays nearly all writing is done on a keyboard. As
        a result of this, even Japanese native speakers have been getting worse
        at writing out kanji from memory.
      </p>
      <p>
        This is why we think it’s completely fine to put off learning to produce
        kanji from memory until after a high level of reading comprehension has
        been acquired. Some learners may choose to never learn how to write out
        kanji by hand, and we think that’s perfectly reasonable as well.
      </p>
      <h2>Kanji Recognition Explained</h2>
      <p>
        Before you start studying Japanese, most kanji aren’t going to look like
        much more than a blob of random scribbles. Of course, some of the more
        pictograph-like&nbsp;characters, like&nbsp;山&nbsp;(mountain)
        or&nbsp;木&nbsp;(tree), are simple and straight forward to
        remember.&nbsp;But,&nbsp;these only represent a tiny fraction of all the
        characters you need to know to be proficient in Japanese.
      </p>
      <p>
        The thing is, nearly all kanji are made up of different combinations of
        the same basic ~200 components. The key to learning kanji is training
        your brain to see them in terms of these components. This way, seemingly
        complicated characters like “露” can be seen as simply
        <em>rain</em> (雨) +&nbsp;<em>path</em>&nbsp;(路)&nbsp;=&nbsp;<em
          >dew</em
        >
        (露). Now, why does <em>rain </em>+ <em>path</em> = <em>dew</em>? Who
        knows; kanji etymology is unclear in more cases than not. Regardless,
        remembering that <em>rain</em> and <em>path</em>&nbsp;combine to
        make&nbsp;<em>dew</em> is a lot easier than remembering a blob of random
        scribbles.
      </p>
      <p>
        As you get more comfortable with seeing kanji in terms of their
        components, learning new kanji will become easier and easier.
        Eventually, you’ll reach the point we call being
        <em>fluent in the meta-language of&nbsp;kanji</em>. Once you reach
        this&nbsp;point, your brain actually stops seeing characters in terms of
        their component parts, and instead starts to see each kanji as a unified
        whole. This is actually how your brain handles words in English as well.
        The reason you’re able to read this text as fast as you are is that your
        brain is able to recognize entire words at once, without needing to pay
        attention to individual letters. That’s why it’s possible to read texts
        such as this:
      </p>
      <blockquote class="bq">
        <p>
          “Aoccdrnig to rscheearch at Cmabrigde Uinervtisy, it deosn’t mttaer in
          waht oredr the ltteers in a wrod are, the olny iprmoetnt tihng is taht
          the frist and lsat ltteer be at the rghit pclae. The rset can be a
          toatl mses and you can sitll raed it wouthit a porbelm. Tihs is
          bcuseae the huamn mnid deos not raed ervey lteter by istlef, but the
          wrod as a wlohe.” (<a
            href="http://www.mrc-cbu.cam.ac.uk/people/matt-davis/cmabridge/"
            target="_blank"
            >source</a
          >)
        </p>
      </blockquote>
      <p>
        In other words, your brain will become able to treat kanji this way.
        Once this happens, recognizing the appearance of a kanji
        will&nbsp;become&nbsp;just&nbsp;like recognizing the face of a
        person.&nbsp;After spending just a few seconds looking at someone for
        the first time, you instantly remember their face. You could go years
        without seeing that person again, and if shown a picture&nbsp;of
        them,&nbsp;their face would likely still feel familiar. You might not
        remember who they are or how you know them,
        but&nbsp;you&nbsp;would&nbsp;know that you’ve seen them somewhere
        before.
      </p>
      <p>
        Now, think about the way you remember a face. Do you memorize the exact
        distance between the person’s eyes and categorize the shape of their
        nose? Of course not. Their face simply has a “look”, and you instantly
        and automatically remember it. Remembering new characters will become
        just like this. As soon as you see a new character, it will look like
        <em>something</em>. When you see that character again, you will
        instantly recognize&nbsp;it as&nbsp;the same character.
      </p>
      <p>
        Now, that being said, this doesn’t necessarily mean that you know what
        the character means or how it’s read. It also doesn’t mean that you can
        write out the character from memory. It simply means that when you see
        the character, you instantly know that it’s
        <em>that</em> character. This is what we mean by “the ability to
        recognize kanji”.
      </p>
      <p>
        It might not&nbsp;seem like much, but in reality, this ability is a
        superpower in the context of learning Japanese. Learning to read and
        understand a word containing a new kanji becomes as simple as, well,
        remembering how it’s read and what it means. You won’t have to deal with
        the additional obstacle of trying to force a blob of random scribbles
        into your memory.
      </p>
      <p>
        And like we explained above, it’s through learning to read and
        understand words that you’ll be able to naturally build an extensive
        mental database of exactly how specific individual characters are read
        and used in different contexts.
      </p>
      <h2>Learning to Recognize the Kanji</h2>
      <p>
        Our recommended method of training kanji recognition is Recognition RTK,
        a modified version of <em>Remembering the Kanji</em> (RTK) combined with
        an SRS.
      </p>
      <p>
        Originally, James Heisig wrote the book
        <em>Remembering the Kanji</em> to help Japanese newcomers become able to
        both recognize and produce the ~3,000 most common kanji. To do this, he
        employed a number of innovative ideas, such as ordering characters based
        on their components (instead of frequency), creating story-based
        mnemonics using those components, and introducing only a single basic
        meaning of each kanji (initially ignoring readings and alternate
        meanings). If you’re interested in learning more about the logic behind
        RTK, check out the extensive intro at the beginning of the RTK part 1
        book.
      </p>
      <p>
        RTK does open up the possibility of learning to both recognize and
        produce kanji as a pure beginner. But, as we explained above, this is
        far more trouble than it’s worth. Indeed, most of the people who attempt
        RTK in its original form give up before making it to the end.
      </p>
      <p>
        Enter Recognition RTK (hereafter “RRTK”). RRTK is an SRS-based
        modification to the original RTK intended to optimize it for training
        recognition. RRTK preserves RTK’s innovative component-based sequencing
        of kanji and story-based mnemonic system. Where it differs is that RRTK
        flashcards have a kanji on the <em>front</em> and the meaning of the
        kanji on the <em>back</em>. While reviewing each card, the goal is to
        <em>recognize</em> the kanji on the front and recall its basic meaning.
      </p>
      <p>
        RRTK also only covers the most frequently used 1,000 kanji. The reason
        for this is that the main goal of RRTK is <em>not</em> to learn what
        every kanji means, but simply to bring learners to the point where
        recognizing a kanji feels similar to recognizing a human face. As soon
        as you reach this point, rather than continuing to study isolated kanji,
        you’re going to be better off jumping into real Japanese and picking up
        more kanji as you go. The idea is that 1,000 kanji should be more than
        enough to take you to this point.
      </p>
      <p>
        Although the main goal of RRTK is to train kanji recognition ability, a
        secondary goal is to help you learn the general meaning of kanji as
        well. This is why RRTK covers the <em>most frequently used</em> kanji,
        instead of simply the first 1,000 kanji in the original RTK book (recall
        that RTK orders kanji based on their components, not their frequency).
        The most frequently used 1,000 kanji comprise more than 90% of all kanji
        used in written Japanese. So, knowing the basic meaning of all of these
        kanji will be hugely beneficial when diving into real Japanese.
      </p>
      <p>
        <strong>NOTE</strong>: Previously, MIA referred to “Recognition RTK” as
        “Lazy Kanji”. “Lazy Kanji” is a term that Khatzumoto from AJATT used to
        refer to his own modified version of RTK, which was similar, although
        significantly different, from RRTK. In order to avoid confusion, we
        decided to change the name to the more straightforward and objectively
        accurate “Recognition RTK”.
      </p>
