      <h2>Warning!</h2>
      <p>
        In order for SRS’s like Anki to really work well, it’s crucial to
        consistently keep up with daily reviews. Every day, Anki will show you
        the cards it thinks you’re likely to soon forget. So naturally, not
        reviewing these cards will lead to increased forgetting. Additionally,
        skipping a day’s reviews also means signing up for double the reviews
        the next day. Because of this, catching up on reviews once you’ve fallen
        behind can be quite a headache. Of course, missing a day every now and
        again isn’t going to be detrimental, but this is only if it’s
        <em>actually</em> every now and again.
      </p>
