      <h1 id="immersion">Immersion</h1>
      <p>
        Immersion refers to any form of listening or reading in your target
        language. As the name Mass <em>Immersion</em> Approach might suggest,
        it’s the absolute most important component of the entire process. Thus,
        acquiring immersion content and beginning to immerse is the very first
        step to learning Japanese with MIA. Immersion then continues throughout
        every step of the process.
      </p>
      <p>
        <strong
          >Even if you don’t know a single word of Japanese, immersion in the
          form of listening will still be of great benefit to you</strong
        >. The reason for this is that, when you first approach a new foreign
        language, your brain won’t yet be able to parse the sounds. The language
        will sound like one big blur of noise, instead of crisp consonants and
        vowels grouped into words. By simply listening to your target language,
        your brain will slowly get better at parsing the sounds. Eventually,
        you’ll start naturally picking out common words. This is the start of
        cultivating listening ability.
      </p>
      <p>
        On the other hand,
        <strong
          >no matter how many words you’ve memorized, without actually listening
          to the language for hundreds of hours, you will understand close to
          nothing</strong
        >.
      </p>
      <p>
        You might be surprised at how much you can learn from simply watching a
        Japanese TV show with no subtitles. Your unconscious mind is a pattern
        recognition machine. Once you start exposing it to Japanese, it will
        immediately get to work decoding the sound system, grammar, and lexicon
        of the language. You’ll slowly start understanding more and more, but
        won’t be able to explain why. This is the counter-intuitive mechanism of
        language acquisition. It might be something that’s hard to fully grasp
        until you’ve started experiencing it for yourself.
      </p>
      <h2 id="listening-vs-reading">Listening vs. Reading</h2>
      <p>
        MIA makes a distinction between <em>listening</em> and <em>reading</em>.
        Listening refers to consuming both strictly audio content, such as
        podcasts and audiobooks, as well as audio-video content, such as TV
        shows and YouTube videos.
      </p>
      <p>
        In the beginning, we recommend focusing on listening rather than
        reading. The reason for this is that, when you first start, you
        inevitably aren’t going to understand much. When you don’t understand
        much, it’s much easier to enjoy and learn from audio/visual content than
        text-based content.
      </p>
      <p>
        That said, watching content with Japanese subtitles is a great way to
        introduce yourself to the written language. As we will talk about later
        in this section, we recommend watching with Japanese subtitles right
        from the beginning. Once you build a foundation in the language, phase
        out Japanese subtitles and replace them with reading fully text-based
        content, such as manga or novels.
      </p>
