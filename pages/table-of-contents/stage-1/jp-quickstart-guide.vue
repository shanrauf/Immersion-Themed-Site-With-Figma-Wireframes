      <h2>How to Use</h2>

      <h3>How do I learn new kanji?</h3>

      <p>
        In order to learn a new kanji, break it down into its component
        primitive elements and come up with a story to link those elements to
        the kanji’s meaning. If one of the stories on the back of the card makes
        sense, go with that. If neither of the stories on the back resonate, you
        can either tweak one of them or come up with something completely new.
        If you need some inspiration, you can check out other people’s stories
        on the
        <a href="https://kanji.koohii.com/">Kanji Koohii website</a>.
      </p>

      <p>
        Feel free to take some liberties when tinkering with the meaning of
        primitive elements and modifying stories. The mnemonics are just a
        pragmatic tool for remembering the characters, so whatever works is
        fine.
      </p>

      <h3>How many new cards a day?</h3>
      <p>
        Depending on how much time you currently have available, we recommend
        learning between 10 and 30 new kanji a day. At a rate of 10 a day, you
        can finish in a little over 3 months. With 30 a day, you can finish in a
        little over 1 month.
      </p>
      <p>
        During RRTK, we recommend allocating around 50% to 75% of your “active”
        time learning and reviewing kanji. Leave around 25%~50% for immersion.
        Also, no matter how much time you have available each day, we don’t
        recommend spending more than 2 hours a day on kanji study.
      </p>
      <h3>How should I grade cards?</h3>
      <p>
        If you could recall the general meaning of the character with relative
        ease, grade the card “good”. If you couldn’t recall the general meaning
        of the character, or you could recall the general meaning but it took
        quite a bit of time/effort, then mark the card “again”. If the character
        has multiple meanings, as long as you could recall at least
        <em>one</em> of those meanings, that’s good enough. Don’t worry about
        recalling the exact RTK keyword. For example, the keyword for “町” is
        “village”. If you were able to recall anything like “town” or “city”,
        that’s good enough.
      </p>
      <p>
        If you didn’t need to use a mnemonic to recall the meaning of a kanji,
        that’s fine. There’s no need to force yourself to recall a mnemonic. If
        you did need to use a mnemonic to recall the meaning of a kanji, that’s
        completely fine too. Whether or not you used a mnemonic to recall the
        kanji shouldn’t affect the grade you give a card.
      </p>
      <h3>Should I write out the kanji?</h3>
      <p>
        It’s a good idea to write out new characters when you initially learn
        them, but we don’t recommend writing out kanji while doing your Anki
        reviews. Because you’re really only after recognition, it’s not going to
        be worth the extra time and effort.
      </p>
      <h3>What should I do about leeches?</h3>
      <p>
        In Anki, a “lapse” is when a learned card is graded “again”. When the
        same card lapses 5 times, Anki will mark it a “leech” and suspend the
        card. Leeches are a natural part of the learning process. Some things
        you learn will stick easily, some won’t stick no matter what you try,
        and most stuff will be somewhere in between. For those small minority of
        cards that just won’t stick, AKA leeches, it’s best to just get rid of
        them. Instead of wasting a bunch of time on a single leech, it’s more
        productive to learn 5 normal cards in its place.
      </p>
      <p>
        It may seem counter-intuitive to delete a kanji out of your deck, but in
        reality, it’s really not a big deal. If you end up knowing 950 of the
        1,000 most frequently used kanji, that’s more than enough to move onto
        the next stage of the learning process. You’ll pick up those missing 50,
        along with thousands of other kanji, naturally as you continue to
        immerse yourself in Japanese. No one word or kanji is going to make or
        break you, and it’s never too late to learn something later.
      </p>
      <h3>
        I finished my Anki reviews for the day but still have time. Should I
        review extra?
      </h3>
      <p>
        We strongly advise against doing so. Anki will tell you exactly how much
        you should review. Reviewing more than this can actually hurt the
        effectiveness of Anki’s algorithm. If you have extra time after
        finishing your kanji work for the day, do more active immersion.
      </p>
      <h3>When should I delete my Recognition RTK deck?</h3>
      <p>
        If you delete your RRTK deck immediately after completing Recognition
        RTK, you’ll likely end up quickly forgetting a significant portion of
        the kanji you learned. In order to avoid this, it’s important to
        continue reviewing your Anki deck after completing RRTK. That said, even
        if you continue diligently keeping up with your daily reviews, after a
        few months after completing RRTK, the retention rate of your deck will
        likely start to decline. This is an inevitable outcome of not
        emphasizing mnemonics while learning the characters.
      </p>
      <p>
        But, this shouldn’t be much of a problem. By the time this starts to
        happen, you should have a strong enough footing in reading immersion
        (watching with Japanese subtitles) to retain the core of your kanji
        ability through immersion alone. Yes, you may forget what a specific
        kanji means here and there, but what’s really important is the ability
        to identify characters.
      </p>
      <p>
        As long as you have a strong footing in reading immersion, it’s fine to
        delete your RRTK deck once the retention rate starts to decline. To be
        specific, we recommend continuing to review your RRTK deck for 3~6
        months after completing Recognition RTK. After that, you can delete your
        deck.
      </p>
      <p>
        Another option is to use the
        <a href="https://ankiweb.net/shared/info/1666520655"
          >MIA Retirement add-on</a
        >
        to set a retiring interval for your RRTK deck. If you take this
        approach, we recommend setting a 9-month retiring interval.
      </p>
      <h2>How Will I Learn Kanji Readings?</h2>
      <p>
        The most pragmatic way of going about learning to read kanji is to
        forget the whole idea that kanji have individual “readings”.
      </p>
      <p>
        In reality, the only time you’ll ever need to know how a kanji is read
        is in the context of reading a word. So, as long as you can read words,
        your kanji reading problem is solved. And like we mentioned above, once
        you have a basic ability to identify characters, you can jump right into
        learning words (exactly how to go about learning words will be discussed
        in later sections).
      </p>
      <p>
        Once you’ve learned to read a few hundred words, you’ll naturally start
        gaining an intuition for how specific characters tend to be read in
        different contexts. Eventually, you’ll find that you can guess the
        readings of new words with high levels of accuracy.
      </p>
      <p>
        For a more in-depth explanation of why you shouldn’t worry about kanji
        readings, watch from 1:53 to 10:55 of
        <a href="https://youtu.be/TgRte6oSoF8?t=113">this video</a>
        (note: Matt has updated his views on some of the conclusions reached in
        this video, as reflected in this guide).
      </p>
      <h1 id="grammar">Basic Grammar</h1>
      <p>
        Once you’ve learned kana and completed Recognition RTK, you’re ready to
        start studying the language formally. In MIA, this takes the form of
        <em>basic grammar</em> and <em>basic vocab</em>.
      </p>
      <h2>The Role of Grammar Study</h2>
      <p>
        In MIA, the goal of studying grammar is strictly to increase the
        comprehensibility of your immersion. In other words, learning the basics
        of Japanese grammar will vastly improve your ability to understand and
        learn from real Japanese, and to this extent, grammar study is
        worthwhile.
      </p>
      <p>
        On the other hand, most language courses (including some of those linked
        below) teach grammar with the intention of helping learners become able
        to
        <em>output</em>, AKA speak and write, their target language.
        Unfortunately, this doesn’t really work. Because
        <nuxt-link
          to="/table-of-contents/stage-1/theory/acquisition-vs-learning/"
          >learned knowledge is largely separate from acquired
          ability</nuxt-link
        >
        and
        <a href="https://youtu.be/y8JK8W8dBxk">language isn’t math</a>,
        conscious grammar study is <em>not</em> an effective way of becoming
        able to speak and write a foreign language.
      </p>
      <p>
        Output ability is the result of subconscious
        <em>acquisition</em>, and this acquisition happens by comprehending
        <em>input</em>, AKA, speech and text, in your target language (this is
        known as the
        <a href="https://en.wikipedia.org/wiki/Input_hypothesis"
          >input hypothesis</a
        >). So, grammar study is useful to the extent that it helps us
        comprehend target language input, but not useful for actually speaking
        and writing the language.
      </p>
      <h2>Theory vs. Practice</h2>
      <p>
        The idea of theory vs. practice is very relevant to the topic of grammar
        study. The following video explains the basic idea:
      </p>

      <YouTubeEmbed src="https://youtube.com/embed/UHAa0mFOsYM" />

      <p>
        In the context of grammar, “theory” refers to “conscious grammar
        knowledge gained through study”, and “practice” refers to “intuitive
        grammar understanding acquired through input”.
      </p>
      <p>
        Basically, there’s a hard limit to how deeply it’s possible to
        understand Japanese grammar without actually acquiring a certain amount
        of Japanese. You could memorize an entire encyclopedia’s worth of
        Japanese grammar theory, but without an ample amount of immersion, that
        knowledge isn’t going to be very useful to you. You will be like someone
        who has read 10 books on how to swim but has never been in a pool.
      </p>
      <p>
        Of course, as we explained above, a certain amount of grammar theory
        will be useful in guiding your practice. But, after a certain point, any
        additional grammar study you do will have greatly diminishing returns.
        The reason for this is that you can’t <em>really</em> begin to
        understand how Japanese grammar actually works until you’ve spent many
        hours exposing yourself to how grammar actually plays out in real
        Japanese.
      </p>
      <p>
        Because of this, when approaching basic grammar, try to make
        <em>quick and dirty</em> your motto. If something’s just not clicking,
        leave it and move on. If something feels unimportant, leave it and move
        on. It’s never too late to revisit something later. Plus, even if you
        don’t, eventually immersion will make everything clear. Like Matt
        explained in the video above, as long as you stick with it, eventually,
        practice will solve all your problems. As evidence for this, you didn’t
        need to study grammar to acquire your native language.
      </p>
      <h2>Resources</h2>
      <p>
        There are many different Japanese grammar resources out there, and
        really, it doesn’t matter which one you use. After all, you’re just
        looking for some
        <em>quick and dirty</em> knowledge anyway.
      </p>
      <p>
        Personally, we recommend
        <a href="http://www.guidetojapanese.org/learn/grammar"
          >Tae Kim’s Japanese Grammar Guide</a
