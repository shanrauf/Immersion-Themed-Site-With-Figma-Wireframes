      <h2>Sentence Mining Q&amp;A</h2>
      <h3>How many cards should I learn a day?</h3>
      <p>
        One way to think about it is the following. Think about many hours you
        want to spend actively working on Japanese each day. Aim to spend one
        third of that time on Anki, one third actively listening, and one third
        reading or watching with Japanese subtitles. For example, if you plan on
        spending 3 hours a day actively working on Japanese, aim to spend 1 of
        those hours on Anki. This includes both creating and reviewing cards.
      </p>
      <p>
        Once you know how long you want to spend on Anki each day, you can use
        the
        <a href="https://pheartheceal.github.io/anki-optimizer/" target="_blank"
          >Anki simulator</a
        >‘s “New Cards Calculator” to calculate how the maximum number of new
        cards you can learn each day without exceeding your desired amount of
        time. For our example, let’s say you spend 15 minutes making new cards
        and 45 minutes reviewing. According to the new card calculator, that
        means you should be able to learn 11 cards a day without ever exceeding
        45 minutes of review time per day.
      </p>
      <p>
        No matter how much time you have, it may be a good idea to start with
        something like 10 cards a day, and see how that feels. If you feel like
        you can handle more, you can begin slowly increasing your amount of
        daily cards. That being said, we don’t recommend ever exceeding 30 cards
        per day, regardless of how much time you have available. For the
        quickest progress, use any remaining time you have on extra immersion.
      </p>
      <p>
        It’s important to remember that
        <strong
          >immersion is the most important component of the language acquisition
          process, and the SRS is simply a supplement to that</strong
        >. Any time spent on the SRS is time <em>not</em> spent on immersion, so
        it’s a good idea to be conservative when deciding how many new cards to
        learn each day.
      </p>
      <h3>
        What should I do when I find a word I want to learn, but don’t have
        access to the original sentence I found it in?
      </h3>
      <p>
        In general, it’s best to learn words in the original sentence you found
        it in. But, sometimes this isn’t possible, due to the original sentence
        not being 1T, or you simply not having access to it. In cases like
        these, consider simply dropping the word and moving on; it will come up
        again in your immersion sooner or later. But, if you really want to
        learn the word, you can try to find an alternate 1T sentence.
      </p>
      <p>You have a few options:</p>
      <ul>
        <li>
          If you have a subs2srs sentence bank, search your bank.
        </li>
        <li>
          Google the word and scroll through random Japanese pages.
        </li>
        <li>
          Search the word on Twitter and scroll through random Japanese tweets.
        </li>
        <li>
          Check an online Japanese dictionary for example sentences (you can
          find a list of dictionaries in the next section).
        </li>
        <li>
          Search one of the following Japanese example sentence websites:
          <a href="http://yourei.jp/" target="_blank">Yourei</a>,
          <a href="http://nlt.tsukuba.lagoinst.info/search/" target="_blank"
            >NINJAL-LWP for TWC</a
          >,
          <a href="http://nlb.ninjal.ac.jp/search/" target="_blank"
            >NINJAL-LWP for BCCWJ</a
          >,
          <a href="https://sentencesearch.neocities.org/" target="_blank"
            >Sentence Search</a
          >.
        </li>
      </ul>
      <h3>
        I want to sentence mine a TV show but I can’t find subtitles for it.
        What should I do?
      </h3>
      <p>
        In cases like this, it’s best to simply give up and find a better source
        to mine from. Unless you’re already at an advanced level, we strongly
        advise against attempting to transcribe apparent 1T sentences from the
        show yourself. You can still watch the show, but just don’t try to mine
        from it.
      </p>
      <p>
        If you catch a word while listening that you want to add to Anki, try
        finding a new 1T sentence using one of the resources listed above.
      </p>
      <h3>
        Sometimes sentences look like they’re 1T, but after looking up the
        target word, I still don’t understand it.
      </h3>
      <p>
        This is completely normal. Just because a sentence
        <em>looks</em> 1T, that doesn’t mean it really is. It could be that the
        sentence in question also uses a grammar structure you aren’t familiar
        with yet, or that one of the other words is being used in a different
        way than you’re used to. Whenever this happens, immediately drop the
        sentence and move on. You should be looking for
        <em>low hanging fruit</em>, AKA, sentences that <em>click</em> as soon
        as you look up the target word. It’s through these sentences that your
        ability grows. So, don’t waste any time trying to figure out a sentence
        that’s not clicking.
      </p>
      <p>
        In general, expect around <em>two thirds</em> of the apparent 1T
        sentence you find to still not make sense after looking up the target
        word. This means that if you’re taking the approach of collecting
        sentences while immersing and then making cards later in bulk, aim to
        collect 3 times as many sentences as you plan on adding to Anki. This
        way, you will be able to afford only keeping the true low hanging fruit.
      </p>
      <h3>
        Should I make cards for things I picked up naturally from immersion?
      </h3>
      <p>
        In general, no. You only have time to make so many new cards each day,
        so you want to make sure you get as much value as possible out of each
        card you make. If you feel like you might be able to remember a word
        without making a card, then don’t make a card. If it turns out that you
        were wrong and you end up forgetting it, it’s never too late to make a
        card later. As a general principle, always lean towards the side of
        making less rather than more cards.
      </p>
      <h3>
        If a card has multiple meanings, should I make a separate card for each
        meaning?
      </h3>
      <p>
        Usually not. Normally, once you know one meaning of a word, remembering
        its other meanings isn’t too difficult. So, when you first learn a word,
        just focus on whatever specific usage is relevant for the sentence you
        found the word in. Then, try to pick up the other meanings over time
        through immersion. It’s ok to make exceptions to this rule if a word has
        two or more radically different usages. But even in those cases, try to
        only learn one usage at a time in order to avoid confusion.
      </p>
      <h3>
        Every day I find more 1T sentences than I need. How do I choose which
        ones to turn into cards?
      </h3>
      <p>
        Ultimately, it doesn’t really matter. In human language, common words
        are used exponentially more than uncommon words. So, most of the unknown
        words you come across are going to be common, and therefore worth
        learning. You may end up learning an uncommon word here and there, but
        that’s not going to hurt you; you’re going to end up learning all of
        those words sooner or later anyway.
      </p>
      <p>
        You don’t need to worry about using frequency lists to measure the value
        of unknown words until you’ve reached an advanced level. Until then,
        it’s best to let your interests guide you. Always pick the sentences
        that look the most intriguing and memorable.
      </p>
      <p>
        Another good rule of thumb is to learn words you feel like you’ve seen
        before. If you feel like you’ve seen a word before, that probably means
        you’ve come across it many times in your immersion, and your brain is
        already primed to learn it. It also means that it must be common and
        high yield.
      </p>
      <h3>How long should sentences be?</h3>
      <p>
        There are no strict rules, but sentence cards are likely to be most
        effective when the sentence is between 3 and 7 words long. Too short,
        and you won’t have enough context for the sentence to be meaningful. Too
        long, and it will be a pain to review the card. Over time, you’ll get a
        feel for the ideal sentence length as you continue to create and review
        sentence cards.
      </p>
      <p>
        We will also note that it’s perfectly fine to make cards out of
        fragments of longer sentences. Just make sure what you have on the front
        of your card is a complete clause.
      </p>
      <h3>Should I put a picture/screenshot on my cards?</h3>
      <p>
        We don’t recommend putting a picture on the front of any of your
        sentence cards. For maximum effectiveness, you want the SRS to reflect
        real life as much as possible. In real life a picture isn’t going to be
        there to remind you what the word means, so you don’t want it on the
        front of your card either.
      </p>
      <p>
        With that said, it can be useful to have images on the back of cards.
        This is especially true for nouns. For example, when learning the word
        for a particular hair style, a picture is going to be much more useful
        than a written description.
      </p>
      <p>
        If you feel that a picture would help you understand a target word, try
        searching Google Images. That said, finding a picture and adding it to a
        card takes time, so don’t feel like you need to find a picture for every
        card. For subs2srs decks that come with screenshots, we recommend
        keeping the screenshots, but having them appear on the back of cards.
      </p>
      <h3>
        When making cards, should I go out of my way to kanjify words?
      </h3>
      <p>
        Some words, such as “ある” and “いる”, can be written in kanji (“有る”,
        “居る”), but are usually written in kana. If you mine a sentence with a
        word that can be written in kanji, but isn’t, it can be a good idea to
        manually convert it to kanji while making the card. The reason for this
        is that if you can read a word’s kanji, you will definitely be able to
        read it in kana as well. On the other hand, just because you can
        understand a word when it’s written in kana, that doesn’t guarentee
        you’ll be able to read its kanji. In other words, better safe than
        sorry.
      </p>
      <p>
        With that being said, there’s no need to kanjify words you already know
        well. For example, it might be a good idea to kanjify “いる” the first
        couple times you make a sentence card that contains it. But, after that,
        you can just keep “いる” as kana for the rest of the cards you make.
      </p>
      <h3>What should I do about leeches?</h3>
      <p>
        In Anki, a “lapse” is when a learned card is graded “again”. When the
        same card lapses 5 times, Anki will mark it a “leech” and suspend the
        card. Leeches are a natural part of the learning process. Some things
        you learn will stick easily, some won’t stick no matter what you try,
        and most stuff will be somewhere in between.
      </p>
      <p>
        For those small minority of cards that just won’t stick, AKA, leeches,
        it’s best to just get rid of them. Instead of wasting a bunch of time on
        a single leech, it’s more productive to learn 5 normal cards in its
        place. As long as you keep immersing, it’s inevitable that you’ll pick
        up those stubborn words sooner or later.
      </p>
      <h3>
        While reviewing, often times I feel like I have simply memorized the
        sentence, and still don’t know the target word.
      </h3>
      <p>
        That’s totally fine. As long as you’ve memorized the sentence (and what
        it means), then, in a sense, you’ve necessarily memorized the target
        word as well. When you come across that word in your immersion, just try
        to recall the original sentence you learned the word in, and you should
        be able to recall the meaning of the word as well. This also connects
        with the next question.
      </p>
      <h3>
        Sometimes I can’t remember the meaning/reading of a word while
        immersing, even though I have a sentence card for it.
      </h3>
      <p>
        This is a normal and inevitable part of the learning process. To learn
        more about why this happens and how to deal with it, watch
        <a href="https://youtu.be/wrBFhsnBQ2k" target="_blank">this video</a>
        (which was linked at the start of the previous section).
      </p>
      <h3>
        So…… I just keep immersing and making sentence cards? That’s it?!
      </h3>
      <p>
        Yup! As explained in the immersion section, active immersion alone is
        enough to take you to fluency; the SRS is just a way of accelerating
        that process. Through regularly getting passive and active immersion and
        consistently creating sentence cards, your Japanese comprehension should
        continue to steadily grow.
      </p>
      <p>
        After making a couple thousand bilingual sentence cards, we recommend
        making the
        <a href="https://www.youtube.com/watch?v=2AH2JmxglzU" target="_blank"
          >monolingual transition</a
        >
        and switching to making monolingual sentence cards.
      </p>
      <p>
        Once you have a strong foundation in Japanese and can comfortably
        function monolingually, we recommend beginning to study pitch accent and
        general Japanese phonetics.
      </p>
      <p>
        Once you start being able to comprehend around 80% of your immersion
        content, you should find that you start feeling ready to start speaking
        naturally. Once that time comes, you can start applying some of the
        techniques Matt talked about in his
        <a href="https://youtu.be/YCESUUA0wL0" target="_blank"
          >How to Start Outputting</a
        >
        video. And that should take you to a high level of Japanese fluency.
      </p>
      <p>
        If you’re curious about when exactly to move on to each of those steps,
        and what lies after basic fluency, check out the
        <a href="/table-of-contents/stage-0/overview/" target="_blank"
          >MIA Overview</a
        >.
      </p>
      <h2>Combining Sentence Mining with Pre-Made Decks</h2>
      <p>
        In the section on basic grammar, the option of going through the Tango
        N4 deck alongside sentence mining was discussed. Although we strongly
        advise against learning strictly from pre-made material, some people
        might find it useful to continue this approach of supplementing sentence
        mining with pre-made decks.
      </p>
      <p>
        As mentioned in the basic grammar section, we don’t recommend above N4
        in the JLPT Tango series. Instead, we consider either a
        <a href="http://rtkwiki.koohii.com/wiki/Core_10k" target="_blank"
          >Core 10k</a
        >
        deck or Dictionary of Japanese Grammar deck to be a better choice.
      </p>
      <p>
        The thing is, if you’re sentence mining and immersing, you’re going to
        end up already knowing a lot of the content in those decks. This means
        that going through them from start to finish would be extremely
        redundant. Because of this, supplementing sentence mining with pre-made
        decks pretty much requires the use of
        <a href="#morphman" target="_blank">MorphMan</a>. MorphMan will help you
        find new content in the pre-made deck that’s 1T for you given your
        current knowledge. This way, you can, for example, learn 10 mined
        sentences and 5 pre-made sentences each day. On days that you’re tight
        on time and can’t make your own cards, you could rely more heavily on
        the pre-made decks.
      </p>
      <p>
        We should note that this sort of approach is completely optional.&nbsp;
        Matt and Yoga just stuck with sentence mining, and it worked out very
        well for them.
      </p>
