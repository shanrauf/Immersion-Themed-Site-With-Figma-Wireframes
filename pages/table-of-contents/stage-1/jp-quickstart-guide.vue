      <h2 id="what-should-i-be-doing">
        What Should I Actually Be Doing While Actively Listening?
      </h2>
      <p>
        Like we mentioned at the beginning of this guide, when you first start
        listening to a foreign language, you won’t even be able to hear where
        one word ends and another begins. So, in the beginning, it can be useful
        to simply pay attention to the sounds of the language, without worrying
        too much about meaning. By actively trying to hear individual vowels and
        consonants as clearly as possible, you’ll speed up the process of your
        brain decoding the Japanese sound system.
      </p>
      <p>
        Once your brain gets a handle on the sound system, many words will
        naturally start popping out at you. Once you reach this stage, try to
        focus on picking out as many individual words as possible. If you hear a
        new word clearly enough that you feel you might be able to look it up,
        consider doing so (see the previous subheading).
      </p>
      <p>
        Additionally, the following video may provide some useful pointers for
        the right mindset to approach immersion with:
      </p>
      <YouTubeEmbed src="https://youtube.com/embed/5xG-WwqhHBc" />

      <p>
        As the video above hints at, learning to
        <em>tolerate ambiguity</em> is an important part of the immersion
        process. As adults, we’re used to understanding nearly everything we’re
        exposed to. Because of this, putting yourself in a situation where
        you’re not entirely sure what’s going on can feel jarring. But this is
        simply par for the course in language learning. The only way to reach a
        point of true understanding is to first go through a lot of
        <em>not</em> understanding. So, learning to make peace with ambiguity is
        an essential skill for a language learner. It can also be a useful
        exercise in learning to feel okay outside your comfort zone in general.
      </p>
      <p>
        Another problem people often run into is that as they improve in their
        target language, they quickly acclimate to their new ability and shift
        their focus back to what they still
        <em>can’t</em> understand. This sort of mindset can be helpful for
        avoiding complacency, but it can also be detrimental to motivation when
        taken too far. In order to counteract this, it’s important to make a
        habit out of actively taking notice of what you <em>can</em> do in your
        target language. Even if you’re only understanding a word or two here
        and there, don’t take that for granted! When you’re consuming real,
        native Japanese, every word you understand is a big deal.
      </p>
      <h2 id="that-sounds-too-simple">
        That’s It? That Sounds Too Simple
      </h2>
      <p>
        The process unfolds in the following way. As you listen, you become able
        to parse the sounds more and more accurately. The more accurately you
        can parse the sounds, the more words will naturally pop out at you while
        listening. Because frequently used words will pop out so often, after
        looking them up once or twice, your memories of what they mean will be
        continually reinforced by more listening. You’ll also end up picking up
        many words from immersion alone, without ever looking them up.
      </p>
      <p>
        As you start understanding all the individual words being spoken, the
        <a
          href="https://en.wikipedia.org/wiki/Language_acquisition_device"
          target="_blank"
          >language acquisition device</a
        >
        in your brain will naturally fill in the blanks for you, and grammar
        will start to intuitively make sense.
      </p>
      <p>
        Through this process, the amount you understand grows. As you get more
        comfortable with the language, it will become easier for you to remember
        new words. Thus, you’ll naturally become able to remember less
        frequently used words as well.
      </p>
      <p>
        If you did nothing other than continue this simple process of listening
        and occasionally looking things up, you would eventually become fluent
        in spoken Japanese. All the other steps of MIA, such as grammar study
        and using an SRS, simply play the role of accelerating this basic
        underlying process.
      </p>
      <h2 id="how-much">How Much Should I Immerse?</h2>
      <p>
        Only you can answer that question. What we can say is that the more you
        immerse each day, the faster you will improve. In fact, larger amounts
        of daily immersion lead to exponentially faster progress. Said another
        way, the more spread out your immersion is throughout time, the more
        total time it’s going to take. Check out
        <a href="https://youtu.be/7MxklQXJbz8" target="_blank">this video</a>
        by BritVsJapan to understand why this is.
      </p>
      <p>
        OK, we know you probably want some actual numbers, so here goes.
      </p>
      <p>
        <strong>DISCLAIMER</strong>:
        <em
          >The following should be considered extremely rough estimates at best.
          At the present moment, we do not have any reliable data on exactly how
          many hours of immersion it takes to reach fluency in Japanese. Also,
          one’s speed of progress is determined by many factors in addition to
          the total number of hours of active and passive immersion.</em
        >
      </p>
      <p>
        If you <em>don’t</em> already speak an Asian language and are learning
        Japanese, with around 5 hours per day of active and 5 hours per day of
        passive immersion, it should take around 2 years to reach fluency. With
        around 3 hours per day of active and 3 hours per day of passive
        immersion, it should take around 4 years to reach fluency.
      </p>
      <p>
        We would estimate that around 2 hours a day of active immersion is
        necessary to make real progress towards fluency. Because there aren’t
        any known examples of people reaching fluency in Japanese with around 1
        hour of immersion a day, it’s hard to say whether it’s possible or how
        long it would take. At the very least, with 1 hour of immersion a day,
        over time it should be possible to build up a basic foundation of
        comprehension ability in the language.
      </p>
      <p>
        With that out of the way, here are some other factors you may also want
        to take into account when deciding how much daily immersion you want to
        aim for:
      </p>
      <ul>
        <li>
          What are your priorities? What in your life is more important than
          Japanese? What is less important? It goes without saying that no
          amount of Japanese ability would be worth sacrificing things like
          health, family, close friends, and other passions. On the other hand,
          some people may decide it’s worth putting aside some of their less
          important pastimes in order to spend more time with Japanese.
        </li>
        <li>
          The more Japanese you do each day, the faster you will improve. The
          faster you improve, the easier it will be to stay motivated. The more
          motivated you are, the more Japanese you will want to do each day.
          This can create a positive feedback loop that greatly aids the
          language learning process.
        </li>
        <li>
          Consistency is key to language learning success. So, we urge you to be
          careful not to bite off more than you can chew and risk burning out.
        </li>
        <li>
          Changing your entire life overnight is a near Herculean feat. Even if
          you plan on doing a hefty amount of immersion each day, it’s probably
          a good idea to start small and slowly ramp up over time. Building a
          consistent habit of 30 minutes of active immersion each day is a great
          place to start. It may also be useful to keep in mind that as your
          Japanese ability grows, immersion will progressively feel less like
          “language learning” and more like “enjoying content”. This will make
          it easier to spend more time with Japanese.
        </li>
        <li>
          By taking advantage of cracks of time in their day, even a fairly busy
          person may be able to rack up a solid number of passive immersion
          hours without making any significant changes to their lifestyle.
          Commuting, cooking, cleaning, exercising, and going to the bathroom
          are all potential opportunities for immersion. Check out the video
          below for some tools that might help you take advantage of these
          opportunities.
        </li>
