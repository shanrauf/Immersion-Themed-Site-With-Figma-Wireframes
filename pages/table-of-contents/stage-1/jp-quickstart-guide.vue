      <h1 id="srs">SRS</h1>
      <p>
        A spaced repetition system, or SRS, generally refers to smart flashcard
        software that uses a scheduling algorithm to optimize study.
      </p>
      <p>
        In order to understand why SRS’s are so powerful, let’s start by
        comparing them to the alternative: paper flashcards. The problem with
        paper flashcards is that you don’t really know when to review which
        cards. Reviewing things you already know well is a waste of time. On the
        other hand, if you don’t review things before you forget them, you end
        up having to waste time on re-learning. This might not really be a
        problem if you’re just trying to pass a test. But, if you’re actually
        trying to retain a large body of knowledge long term, you’re going to
        need a more advanced tool.
      </p>
      <p>
        The scheduling algorithm found in SRS’s solve this problem. Using an
        algorithm based on human memory, combined with feedback that you give
        it, an SRS is able to predict the ideal time for you to study each
        flashcard you put into the system. Based on these predictions, you’re
        automatically shown what specific cards to study each day. This allows
        you to continue to retain knowledge with minimal time wasted on
        unnecessary reviews and re-learning things you forgot. Used correctly
        over the long term, an SRS can allow you to commit tens of thousands of
        items to memory with only minutes of reviewing each day.
      </p>
      <h2>Which SRS Should I Use?</h2>
      <p>
        There are currently many different SRS’s available. But, in our opinion,
        there are only two really worth taking seriously: Anki and SuperMemo.
      </p>
      <p>
        SuperMemo was the first SRS ever invented, and it has continued to
        receive upgrades since its original release in 1985. It has by far the
        most advanced algorithm and the largest number of features. The problem
        is that it’s extremely quirky and difficult to use. It’s also only
        available for Windows (meaning it can’t be used online, on a Mac, or on
        a smartphone).
      </p>
      <p>
        This is why we recommend Anki. Anki is a versatile SRS with a large
        selection of features. The
        <a href="https://apps.ankiweb.net/" target="_blank">desktop version</a>
        is available for Windows, Mac and Linux, there’s a fully
        <a href="https://ankiweb.net/" target="_blank">online version</a>, and
        there are smartphone apps for both
        <a
          href="https://apps.apple.com/us/app/ankimobile-flashcards/id373493387"
          >iOS</a
        >
        and
        <a
          href="https://play.google.com/store/apps/details?id=com.ichi2.anki&amp;hl=en_US"
          target="_blank"
          >Android</a
        >. You can create an account and sync your collection freely between
        each of these platforms.
      </p>
      <p>
        All versions of Anki are 100% free, with the exception of the iOS app,
        which costs 25 USD. The creator of Anki chooses to only charge money for
        the iOS app in order to keep the desktop version free (the Android app
        was created by a third party). If you’re an Apple user and planning on
        using Anki, the app will likely be well worth the cost in the long term.
      </p>
      <p>
        The great thing about Anki is that it allows you to install user-created
        add-ons to add custom features. There are many add-ons available that
        greatly aid Japanese study, including a few MIA originals. For a
        language learner, these add-ons are really what seals the deal for
        choosing Anki. You can find a list of public add-ons
