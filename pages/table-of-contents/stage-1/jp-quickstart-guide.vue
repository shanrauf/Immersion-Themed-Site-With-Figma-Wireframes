      <h1 id="sentence-cards">Sentence Cards</h1>
      <p>
        After completing basic grammar and basic vocab, the bulk of your active
        study will revolve around sentence cards until reaching basic fluency.
        In this section, we’ll explain what sentence cards actually are, and in
        the next section, we’ll explain how to make them.
      </p>
      <p>
        A sentence card is an SRS card with a target-language example sentence
        on the front, and whatever is needed to understand the sentence on the
        back. Often times, sentence cards are made using sentences taken
        directly from your own immersion.
      </p>
      <p>
        In our opinion, sentence cards are the most effective way to use an SRS
        for vocabulary acquisition. We will explain exactly why we think this is
        below, but before moving on, we recommend watching the following video
        for more context. It explains how learning words with the SRS fits into
        the larger language acquisition process:
      </p>
      <YouTubeEmbed src="https://youtube.com/embed/wrBFhsnBQ2k" />

      <h2>Why Sentence Cards are Effective</h2>
      <p>
        In order to understand why sentence cards are effective, let’s start by
        thinking about what the goal of learning words actually is. Like we’ve
        been hinting at throughout this guide, in MIA, your first goal is to
        become able to
        <em>understand</em> real Japanese. If you can’t even understand
        Japanese, you’re not likely to have much luck with producing it
        yourself. On the other hand, once you do become able to understand real
        Japanese, with just a little practice, the ability to speak and write
        will come naturally.
      </p>
      <p>
        So, just like with grammar, our goal in learning words with the SRS is
        to increase our ability to understand Japanese. Because of this, the
        most effective SRS cards are those that allow us to actually practice
        <em>understanding Japanese</em>. This is precisely what a sentence card
        does.
      </p>
      <p>
        In order to explore this idea more deeply, let’s compare sentence cards
        to another popular SRS card format: isolated vocab cards. The most
        common form of isolated vocab cards have a single target language word
        on the front, and its English meaning on the back. The main problem with
        this is that most Japanese words don’t have a true English equivalent.
        So, simply reading the English definition of a Japanese word won’t be
        enough to grasp what the word really means in context.
      </p>
      <p>
        For example, if you look up the Japanese word “暇”, the Japanese to
        English dictionary says it means, “spare time; free time; leisure”. But,
        this alone doesn’t really get across the reality of “暇”. On the other
        hand, if you made a sentence card with the sentence “いま暇ある？” on
        the front, and the English translation “Are you free right now?”, along
        with the English definition of “暇” on the back, that would give you a
        much better idea of how “暇” actually functions in Japanese.
      </p>
      <p>
        Another problem with isolated vocab cards is that many Japanese words
        have more than one meaning. For example, if you look up the Japanese
        word “<a href="https://jisho.org/search/%E3%81%8B%E3%81%91%E3%82%8B"
          >掛ける</a
        >” in the Japanese to English dictionary, the definition listed is
        comically long. Imagine making a flashcard with “掛ける” on the front,
        and that long list of possible meanings on the back. Not only would
        trying to recall all of those different meanings be a nightmare, but you
        still wouldn’t really understand what “掛ける” actually does in real
        Japanese.
      </p>
      <p>
        On the other hand, if you made a sentence card with
        “眼鏡を<strong>掛ける</strong>” on the front, and the English
        translation “<strong>Put on</strong> glasses”, along with the English
        definition of “掛ける” on the back, that would at least give you a clear
        understanding of one of the meanings of “掛ける”. Then, if you also
        learned that the sentence “彼の肩に手を<strong>掛けた</strong>” meant
        “He <strong>placed</strong> a hand on his shoulder”, you may start to
        intuitively grasp what “掛ける” actually means.
      </p>
      <p>
        We could probably write thousands of more words on why sentence cards
        are effective, but for the sake of brevity, here is a bullet-point list
        of some of the additional benefits of sentence cards:
      </p>
      <ul>
        <li>
          A sentence tells you not only what a word means, but how it’s used.
          Check out
          <a href="http://www.antimoon.com/how/examplesent.htm">this article</a>
          for a more in-depth explanation of this point.
        </li>
        <li>
          A sentence is a larger unit of meaning than an isolated word, so it’s
          much easier to understand sentences without
          <a href="https://youtu.be/CRmxNFv04ao">translating in your head</a>
          than isolated words.
        </li>
        <li>
          It’s easier to judge whether you understood a sentence than to judge
          whether you understood an isolated word.
        </li>
        <li>
          Sentences make things easier to remember. A sentence like “He felt
          happiness for the first time in a long while” leaves a much deeper and
          more meaningful impression than simply “to feel”. The more
          impressionable something is, the less likely you are to forget it.
        </li>
        <li>
          Because “reading and understanding a sentence” replicates how we use
          language in real life more than “recalling the meaning of an isolated
          word”, knowledge gained through sentence cards will transfer to actual
          ability more easily than knowledge gained through isolated vocab
          cards.
        </li>
        <li>
          Language is acquired through comprehending messages, so a sentence
          card makes it more likely that a word will actually be
          <em>acquired</em>, not just <em>learned</em>.
        </li>
      </ul>
      <h2>One-Target vs. Multi-Target Sentences</h2>
      <p>
        In order for a sentence card to be effective, it’s important that the
        sentence on the front be a one-target (1T) sentence. This means there’s
        only one unknown word or grammar structure being introduced in the
        sentence. You know a sentence is 1T when, after looking up or inferring
        the meaning of the unknown word/structure, the meaning of the entire
        sentence becomes clear.
      </p>
      <p>
        If a sentence is zero-target (0T), AKA, doesn’t contain anything you
        don’t already know, you won’t gain anything by making it into a sentence
        card. On the other hand, if a sentence is multi-target (MT), AKA,
        contains multiple words/structures you don’t already know, it’s going to
        be very difficult and frustrating to learn.
      </p>
      <p>
        Learning through 1T sentences can be thought of as “picking low hanging
        fruit”. It makes the target word/structure easy to understand and
        retain. As you continue to learn, sentences that previously were 1T will
        become 0T, and sentences that previously were MT will become 1T. In this
        way, 1T sentences can take you all the way to fluency.
      </p>
      <p>
        <strong>NOTE</strong>: Previously, MIA referred to “1T sentences” as
        “i+1 sentences”. The term “i+1” comes from Stephen Krashen and his Input
        Hypothesis. From
        <a href="https://en.wikipedia.org/wiki/Input_hypothesis">Wikipedia</a>:
        “[The Input Hypothesis] states that learners progress in their knowledge
        of the language when they comprehend language input that is slightly
        more advanced than their current level. Krashen called this level of
        input “i+1”, where “i” is the learner’s interlanguage and “+1″ is the
        next stage of language acquisition.” In other words, Krashen’s “i+1”
        refers to input that is immediately comprehensible to the unconscious
        language acquisition device. On the other hand, MIA was using the term
        to refer to input that could be consciously comprehended after looking
        up or inferring the meaning of an unknown word or structure. In order to
        avoid confusion due to this slight difference in meaning, we decided to
        drop the term and adopt the more straightforward “one target” instead.
      </p>
      <h2>Different Types of Sentence Cards</h2>
      <p>
        There are a few different types of sentence cards. The main categories
        are as follows:
      </p>
      <h3>Bilingual Sentence Card</h3>
      <p>
        A sentence card with a target language sentence on the front, and an
        English translation/definition on the back.
      </p>
      <p>
        Once you’re past the basic vocab phase,
        <strong
          >putting full English translations on the back of cards is not
          recommended</strong
        >. The reason for this is that English translations will always slightly
        differ from the original Japanese nuance. So, referencing an English
        translation can negatively bias the way your brain interprets the
        original Japanese. Thus, it’s best to stick to just putting the English
        definition of the target word/structure. If the sentence is 1T, that
        should be enough to understand it.
      </p>
      <p>
        Bilingual sentence cards are used until the monolingual transition, at
        which point they are replaced by monolingual sentence cards.
      </p>
      <h3>Monolingual Sentence Card</h3>
      <p>
        A sentence card with a target language sentence on the front, and a
        monolingual definition of the target word/structure on the back. In
        other words, you’re using a monolingual dictionary created for native
        speakers of your target language (in the case of Japanese, a J-J
        dictionary).
      </p>
      <p>
        Monolingual cards are powerful because they allow you to learn your
        target language in your target language.
      </p>
      <h3>Text-based Sentence Card</h3>
      <p>
        A sentence card with a written sentence on the front, and whatever is
        needed to understand the sentence on the back. Could be monolingual or
        bilingual. In the case of Japanese, the sentence on the front should be
        full kanji with no furigana. Text-based sentence cards do not have any
        audio on the front of the card. It can be useful to have native audio
        for the sentence on the back of text-based sentence cards, but it is not
        necessary.
      </p>
      <h3>Audio-based Sentence Card</h3>
      <p>
        A sentence card with the native audio of a sentence on the front, and a
        written form of the sentence plus everything needed to understand it on
        the back. Could be monolingual or bilingual. Audio-based sentence cards
        do not have any text on the front of the card.
      </p>
      <h2>Text-based vs. Audio-based Sentence Cards</h2>
      <p>
        Both text-based and audio-based sentence cards have their own strengths
        and weaknesses.
      </p>
      <p>
        The strength of text-based cards is that they’re great for training
        reading ability. The reason for this should be pretty self-explanatory:
        the cards have you actually <em>read</em> a sentence. The weakness of
        text-based cards is that they don’t translate into listening ability
        quite as well as audio-based cards. Just because you can read a word,
        that doesn’t guarantee that you will be able to successfully pick it out
        from native speech.
      </p>
      <p>
        The strength and weakness of audio-based cards are just the opposite.
        Audio-based cards are great for training listening ability, as cards
        have you understand a spoken sentence. On the other hand, they don’t
        translate into reading ability as well, as they don’t actually test your
        ability to read the sentence. That said, with audio-based cards you do
        see the written form of the sentence on the back of the card, which does
        create reading ability benefits. In fact, many people report finding it
        much easier to learn to <em>read</em> a word they have an audio-based
        card for than it is to learn to <em>hear</em> a word they have a
        text-based card for.
      </p>
      <p>
        Another difference between the two is that text-based cards are
        generally easier to make. All you need is the text for a sentence, so
        they can easily be made from things like webpages, tweets, YouTube
        comments, manga, novels or subtitles. On the other hand, audio-based
        cards require procuring audio files for sentences, which can be
        difficult. Also, with text-based cards, you can get away with being less
        strict about the 1T principle. This is because you can read the sentence
        as slow as you want, and take your time understanding the meaning. This
        allows you to push your limits a bit. On the other hand, audio has
        timing baked in. So, with audio-based cards, if you listen to the audio
        and can’t understand it, then you can’t understand it, and there isn’t
        much you can do.
      </p>
      <p>
        Our recommendation is that, if you’re generally more focused on
        cultivating listening ability, try to make more audio-based cards. If
        you’re generally more focused on cultivating reading ability, try to
        make more text-based cards. With that said, you definitely don’t have to
        stick to either one or the other. We recommend making a combination of
        both, and getting a feel for which one you like more.
      </p>
      <h2>Reviewing Sentence Cards</h2>
      <p>
        When reviewing a sentence card, your job is to read/listen to the
        sentence and <em>understand</em> it. If it’s a text-based card, make
        sure you can also read all of the kanji in the sentence.
      </p>
      <p>
        For text-based cards, you don’t have to read the sentence out loud, but
        you can if you want to. For audio-based cards, you don’t have to repeat
        the sentence out loud, but you can if you want to. For both text-based
        and audio-based cards, you don’t have to write out any part of the
        sentence, and we would recommend against doing so, as it would add too
        much time to your review sessions.
      </p>
      <p>
        Do not try to translate the sentence into English. Try to actually
        understand it in Japanese (for tips on how to do this, check out
        <a href="https://youtu.be/CRmxNFv04ao">this video</a>. If you find
        yourself accidentally translating into English, that’s fine. Just don’t
        make an active effort to do so. Also, don’t try to recall the exact
        definition of the target word. Just focus on understanding the actual
        sentence, and getting a feel for what role the target word is playing in
        the sentence.
      </p>
      <p>
        Don’t get caught up in understanding the sentence perfectly. If you feel
        like you pretty much get what’s going on, that’s good enough. That being
        said, get rid of any cards that confuse you, as they aren’t useful and
        make the reviewing process more frustrating.
      </p>
      <p>
        If you felt like you understood the sentence well, you don’t have to
        read the back of the card. Treat the back of the card as a reference,
        which you only refer to when necessary.
      </p>
      <h2>Grading Sentence Cards</h2>
      <p>
        In Anki, when you grade a card “good”, what’s really happening is that
        you’re telling Anki you want to see the card less often. When you grade
        a card “again”, you’re telling Anki you want to see the card more often.
        So, when grading cards, instead of asking yourself the question, “did I
        remember/understand it correctly?”, we think it’s more useful to ask,
        “would I benefit more from seeing this card more often?” If the answer
        is “no”, then grade the card “good”; if the answer is “yes”, then grade
        the card “again”.
      </p>
      <p>
        This means that sometimes you might grade a card “good” even if you
        forgot part of it. For example, perhaps you just had a brain fart, or
        the reading of a word isn’t sticking no matter how many times you see
        it. In both of these cases, you probably wouldn’t benefit from seeing
        the card more often.
      </p>
      <p>
        We recommend sticking with the “good” and “again” buttons. The “hard”
        button in particular has counter-intuitive negative effects on Anki’s
        algorithm. To understand why this is, check out our article series on
        <nuxt-link to="/table-of-contents/anki/low-key-anki/intro/"
          >Low-Key Anki</nuxt-link
        >.
      </p>
      <p>
        In general, don’t get too worked up about grading your cards correctly.
        Anki’s algorithm is pretty inexact anyway. Even if you occasionally
        grade a card poorly and end up forgetting it, nothing is really lost.
        You can always re-learn cards the next time they come up. Plus, in the
        grand scheme of things, you can always trust immersion to fill in any
        important gaps in your knowledge.
      </p>
      <h1 id="sentence-mining">Sentence Mining</h1>
      <p>
        Sentence mining is the process of finding 1T sentences in your immersion
        and making them into sentence cards. Sentence mining is extremely
        powerful because it integrates study with immersion, guaranteeing that
        you’re always learning what’s most relevant to you.
      </p>
      <p>
        Below, we’ll explore a few different ways of going about sentence
        mining. Some are simple, and others are more complicated. We recommend
        experimenting with different methods until you find a sentence mining
        workflow that works well for you. Feel free to get creative! Also, if
        some of the complicated methods sound intimidating, feel free to ignore
        them and stick with the straight forward ones. What method you use
        doesn’t really matter; what really matters is that you’re creating Anki
        cards out of sentences from your immersion.
      </p>
      <h2>Basic Sentence Mining</h2>
      <p>
        Its essence, sentence mining is extremely simple. Just spot a 1T
        sentence in your immersion, copy/paste or manually type it into an Anki
        card, look up the target word in an online dictionary, and copy/paste it
        onto the back.
      </p>
      <p>
        Matt and Yoga used variations of this basic technique for the entirety
        of their Japanese learning career. They had great success with it, and
        many others have as well.
      </p>
      <p>
        Here are some tips for getting the most out of this style of sentence
        mining:
      </p>
      <ul>
        <li>
          While immersing, copy all of the potential 1T sentences you find into
          a text file. Later, turn the sentences you collected into Anki cards
          in bulk. This way, you don’t have to constantly interrupt the flow of
          your immersion.
        </li>
        <li>
          When immersing with things like video games and manga, take pictures
          of potential 1T sentences with your phone, and then transcribe them
          later in bulk.
        </li>
        <li>
          E-readers like
          <a href="https://youtu.be/1CrlQHA9i0g">Kindle</a>
          let you easily highlight and save sentences while reading, which you
          can then export to your computer for card making.
        </li>
        <li>
          <a href="https://youtu.be/IS7WzYICAsk">ShareX</a>
          allows you to easily record short segments of audio in real-time. This
          is useful for audio-based sentence cards.
        </li>
      </ul>
      <p>
        When using this sort of approach to sentence mining, don’t think of
        “normal immersion” and “finding 1T sentences” as two separate
        activities. Rather, whenever you’re immersing in Japanese, be on the
        lookout for potential 1T sentences to mine. That said, as was mentioned
        above, we do recommend dedicating a separate period of time to
        <em>creating</em> cards out of the sentences you’ve collected.
      </p>
      <p>
        Once you’ve hit your sentence quota for the day, you can stop collecting
        new sentences. With that being said, avoid falling into the trap of
        using this as an excuse to not pay attention to your immersion.
      </p>
