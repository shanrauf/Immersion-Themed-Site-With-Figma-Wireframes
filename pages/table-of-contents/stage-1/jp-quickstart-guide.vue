      <h3>
        1,250 Cards for 1,000 Kanji
      </h3>

      <p>
        Like we mentioned earlier, one of RTK’s most powerful innovations was
        sequencing characters based on their components, such that each
        character learned can be viewed as a simple combination of familiar
        elements. RRTK makes use of this methodical sequencing to help users
        learn to recognize the most common 1,000 kanji. That said, applying
        RTK’s original sequencing to RRTK wasn’t as straightforward as trimming
        RTK’s kanji list down to the most common characters.
      </p>

      <p>
        Within the original RTK book, components of kanji are referred to as
        “primitive elements”. Please note that RTK’s primitive elements do not
        align with traditional kanji
        <a
          href="https://en.wikipedia.org/wiki/List_of_kanji_radicals_by_frequency"
          target="_blank"
          >radicals</a
        >. To go back to the example from above, RTK breaks down the character
        “露” (dew) into the primitive elements “雨” (rain) and “路” (path). In
        this case, both primitive elements also exist as independent kanji.
        However, there are also primitive elements that do not exist as
        independent kanji, and are solely used as components of other
        characters. For example, the primitive element "宀" is used inside many
        kanji (for example, "家" (house)), but isn't actually a kanji itself.
      </p>

      <p>
        Oftentimes, common kanji are composed of fairly uncommon component
        characters. For example, the extremely common kanji “頭” (head) contains
        within it the kanji “頁” (page), which is rarely used. Because of this,
        reducing RTK to strictly the most common 1,000 characters would be quite
        problematic. Uncommon kanji that function as primitive elements would
        end up getting removed, which would in turn make learning characters
        that contain those primitive elements much more difficult.
      </p>

      <p>
        In order to remedy this, in addition to the 1,000 most common kanji, the
        RRTK deck also contains cards for an additional 250 primitive elements.
        Some of these primitive elements exist as independent kanji, while
        others are used exclusively as components of other characters. Although
        these primitive elements are not actually common kanji themselves,
        because they’re also <em>components</em> of common kanji, learning them
        is necessary in order to fully benefit from RTK’s original mnemonic
        system.
      </p>

