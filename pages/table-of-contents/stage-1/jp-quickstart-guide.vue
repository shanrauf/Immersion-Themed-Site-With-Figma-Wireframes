        without going into too much detail.
      </p>
      <p>
        If for whatever reason Tae Kim’s guide just really doesn’t sit well with
        you, some other resources you might check out are
        <a href="https://www.imabi.net/tableofcontents.htm"
          >IMABI’S Japanese Grammar Guide</a
        >
        (thorough and detailed) or
        <a
          href="https://www.youtube.com/playlist?list=PLOcym2c7xnBwU12Flkm5RcLIEhvURQ8TB"
          >Japanese from Zero</a
        >
        (extremely user-friendly).
      </p>
      <p>
        <strong>WARNING</strong>: The resources above may contain what MIA
        considers to be ineffective language learning advice. We recommend
        making use of their grammar explanations but disregarding their language
        learning advice.
      </p>
      <h2>How to Use</h2>
      <p>
        If you go with Tae Kim, we recommend reading through the basic grammar,
        Essential Grammar, Special Expressions, and Advanced Topics sections.
      </p>
      <p>
        Don’t try to memorize anything; just read through, and see what sticks.
        Keep in mind that the goal of grammar study is to increase
        comprehension. This means you don’t need to worry about things like all
        the different types of verbs and exactly how they conjugate. As long as
        you can recognize the different forms when you see them, that’s all you
        need. For example, as long as you can recognize when a verb is in the
        te-form, then you can understand it. The rules for how to conjugate
        verbs into the te-form aren’t necessary. Once you’ve gotten enough
        immersion, you’ll become able to conjugate into the te-form intuitively.
      </p>
      <h3>Should I make Anki cards?</h3>
      <p>
        You can, but we don’t recommend it. Like we said above, when it comes to
        grammar, some stuff will click, and other stuff won’t. If you’re
        regularly immersing, you won’t need Anki to remember the stuff that
        clicks. And if something’s just not making sense, creating Anki cards
        for it is just going to be frustrating.
      </p>
      <p>
        If you were going to make Anki cards for Tae Kim, the best way to go
        about doing so would be to make sentence cards out of the example
        sentences. A sentence card is an SRS card with an example sentence on
        the front, and whatever is needed to understand the sentence on the
        back. When reviewing the card, your job is simply to read and understand
        the sentence. Sentence cards will be explained in-depth later on in the
        guide.
      </p>
      <p>
        Making sentence cards out of Tae Kim’s example sentences would help you
        consolidate grammar and vocabulary at the same time. The problem is that
        many of Tae Kim’s example sentences introduce multiple new words at the
        same time. Sentence cards that contain more than one new word are
        generally very difficult and frustrating to learn. Because of this, we
        recommend a different way of using the SRS to consolidate basic grammar
        and vocab, which will be discussed in the next section.
      </p>
      <h3>
        How much time should I spend studying basic grammar each day?
      </h3>
      <p>
        It’s up to you, but we would recommend around 15 minutes. We all know
        that reading about grammar isn’t exactly the most exciting thing in the
        world. Plus, consuming grammar theory incrementally over time will make
        it easier to consolidate and integrate.
      </p>
      <p>
        If you take the approach of only spending ~15 minutes a day studying
        grammar, it’s completely fine to do basic grammar alongside Recognition
        RTK and/or basic vocab (which will be discussed in the next section).
      </p>
      <h1 id="vocab">Basic Vocab</h1>
      <p>
        In Japanese, the most frequently used 1,000 words comprise around 75% of
        all spoken and written language (<a
          href="https://pj.ninjal.ac.jp/corpus_center/bccwj/freq-list.html"
          >source</a
        >). Similar statistics are true for every human language. Because of
        this, memorizing the most frequently used 1,000 words is a great way to
        jumpstart your comprehension when beginning to learn a new language. In
        MIA, this is referred to as
        <em>basic vocab<em>. </em></em>
      </p>
      <p>
        Naturally, the SRS is the perfect tool for this task. The question then
        becomes, what kind of SRS cards should be used? Our recommendation is
        sentence cards. As mentioned in the previous section, a sentence card
        has an example sentence on the front, and whatever is needed to
        understand the sentence on the back. When reviewing the card, your job
        is simply to read and understand the sentence. Sentence cards will be
        discussed in-depth in the next section. If you have a question related
        to basic vocab that isn’t answered in this section, some of them may be
        answered there.
      </p>
      <p>
        Although sentence cards are the ideal way to use an SRS to learn vocab,
        in practice, this can be difficult to do as a beginner. The thing is, in
        order for sentence cards to be effective, it’s important that each card
        only introduces a single unknown word. But, for a beginner who doesn’t
        know many words, finding these kinds of sentences is going to be near
        impossible.
      </p>
      <p>
        Usually, this leaves you with two options for getting past the basic
        vocab stage. Either you can learn sentences with multiple unknown words
        in them, or you can make isolated vocab cards (which will be discussed
        in the next section). Neither option is ideal, but isolated vocab cards
        are the better of the two. The reason for this is that sentence cards
        with multiple unknown words are usually extremely difficult and
        frustrating to learn.
      </p>
      <p>
        But, in the case of Japanese, there’s a resource available that makes it
        possible to easily learn vocab through sentences right from the start.
      </p>
