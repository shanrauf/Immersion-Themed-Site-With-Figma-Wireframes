      <h1 id="considerations">Considerations</h1>
      <p>
        Before I jump into the details of the different categories, let me touch
        on a few broader points about the model:
      </p>
      <ul>
        <li>
          In order to be applicable to both native speakers and learners alike,
          the model is set up in such a way that the average native speaker
          won't receive a perfect score in any category. For the model to be
          comprehensive, a perfect score must signify the hypothetical peak of
          ability in that category, so it wouldn't make sense to base it on the
          level of the average speaker. For example, we would expect a
          professional voice actor to be able to give a much better vocal
          performance than someone with no vocal training, so if the model was
          calibrated so that the average person received a perfect score in
          Delivery, then the model wouldn't be able to quantify the
          <em>difference</em> in ability between a professional voice actor and
          the average person.
        </li>
        <li>
          Each category of ability is comprised of a myriad of smaller, more
          specific abilities (micro-abilities), which can each develop
          independently from one another. This means that rating a given aspect
          may not always be completely straightforward. An extreme example might
          be someone who is a phenomenal public speaker but profusely stutters
          in one-on-one conversations. How would we rate their Delivery?
          Although it may be tricky, in cases like these, try to average the
          major micro-abilities that comprise the category to come up with an
          overall score.
        </li>
        <li>
          As a category of ability develops, the range of micro-abilities that
          comprise that category become broader. For example, at the lower
          levels, Phrasing develops along a relatively straightforward path
          (becoming increasingly able to use words and phrases in natural and
          idiomatic ways). But at the higher levels, when Phrasing becomes about
          using words in the most skillful or elegant ways possible, development
          branches off into countless specific domains of language, such as
          giving a formal speech, creating an engaging YouTube video, or being a
          standup comedian.
        </li>
        <li>
          Development at these highest levels is mainly about truly excelling in
          the limited number of domains most relevant to the person. Even if
          other domains that aren't relevant to the person remain at a more
          average level, that shouldn't negatively affect their rating. This may
          seem to contradict what I said above, but the difference is<em>
            whether a micro-ability is relevant to the person or not</em
          >. Everyday conversation is a domain that is relevant to nearly
          everyone, so if someone can't speak to other people face-to-face, that
          would negatively affect their score. On the other hand, if a
          world-class motivational speaker couldn’t control their voice as well
          as a standup comedian, that wouldn't affect their score, as that
          specific micro-ability wouldn't be required by any of the domains
          relevant to the person.
        </li>
        <li>
          Reaching the highest levels of development in a certain category may
          require development in other categories as a prerequisite. For
          example, a certain level of development in Pronunciation is necessary
          to reach the highest levels of Delivery possible.
        </li>
        <li>
          Although the model breaks language ability down into various
          categories, that isn't meant to imply that each category is equally
          important, or that someone must be at a high level in every category
          to be considered a competent user of a language. The relative
          importance of each category will differ depending on the specific
          goals and priorities of the individual, and in some cases, certain
          categories may be given little to no importance at all.
        </li>
        <li>
          Applying the model to specific individuals will require a significant
          amount of subjective judgment. This is inevitable, as there is no
          ultimate authority to refer to when thinking critically about language
          use, and much of what people find "skillful," "ideal," or "good" boils
          down to personal preference.
        </li>
        <li>
          At the end of the day, it's just a model. By definition, a model is a
          <em>representation</em> of reality, so some amount of detail is
          inevitably going to be lost when compared to reality itself. The model
          is meant to be a pragmatic tool for thinking about language ability,
          not the end-all-be-all description of someone's capacity to use
          language.
        </li>
        <li>
          I am sure that the model has many flaws that I was not able to
          foresee. I plan on continuing to update the model as these flaws are
          made aware to me.
        </li>
      </ul>
