      <h1 id="intro">Intro</h1>
      <p>
        As language learners, we need to be able to talk about different levels
        of ability. It's what allows us to set concrete goals, measure how
        effective different methods are, and determine how qualified someone is
        to give advice. Yet, in my opinion, we still don’t have an adequate
        system for discussing language ability in a nuanced and technical way.
      </p>

      <p>
        At this point, it's already a cliché to complain that the terms
        "fluency" and "native level" are too vague to be helpful. Everyone's
        definition of "fluency" is different, and not all native speakers are at
        the same level of ability. Systems such as CEFR or JALUP's "Levels" are
        big steps up, as they provide a full continuum of well-defined levels,
        rather than a simple binary. But these systems are still extremely
        limited in that they don't account for the multi-faceted nature of
        language ability. What if you can write academic articles in a language,
        but don't have enough verbal fluency to hold a conversation? What if
        you're a native speaker who never learned how to read? What if your
        overall linguistic competence exceeds most native speakers, but you
        speak with a thick foreign accent? These models can't account for such
        imbalances in the different aspects of ability. And because almost
        everyone is imbalanced in one way or another, this constitutes a big
        problem.
      </p>

      <p>
        And this problem isn't easy to solve, as there is no obvious way of
        defining what counts as a separate "aspect" of language ability. For
        example, we could break "speaking ability" into "pronunciation,"
        "grammar," and "word choice." We could also further divide
        "pronunciation" into "timbre" (pronunciation of vowels and consonants),
        "stress accent/pitch accent/tones," and "intonation". Or maybe "cadence"
        and "flow" should be given their own categories as well. And maybe how
        much "flow" someone can speak with depends on how familiar they are with
        a topic, so we might want "flow when speaking about a familiar topic"
        and "flow when speaking about an unfamiliar topic". Or do we need "flow
        when speaking about a semi-familiar topic" as well? As you can see,
        there is a virtually unlimited number of different categories that we
        could hypothetically slice and dice language ability into. The more
        distinct categories we create, the greater detail we will be able to
        describe language ability with. But more distinct categories also mean a
        more complicated and convoluted model. If the model is too complicated,
        it will be confusing, cumbersome, and therefore effectively useless.
      </p>

      <p>
        The problem is further complicated by the fact that many aspects of
        "language ability" exist on a continuum alongside skills that are
        seemingly unrelated to language. For example, it's hard to draw a clear
        line between "speaking ability" and "social skills," or "vocabulary" and
        "knowledge" (is not knowing the difference between a "lunar eclipse" and
        a "solar eclipse" an issue of scientific knowledge, vocabulary, or
        both?) We also must take into account that judgments about linguistic
        ability are often inherently subjective. For example, let's say we
        decide that someone who strings words together in a creative and elegant
        way is "better" at speaking than the average person… How exactly do we
        measure how "creative" and "elegant" someone's speech is?
      </p>

      <p>
        After taking all the above into consideration, I created what I call the
        "6-Point Model of Language Ability." In this model, language ability is
        first broken down into "spoken language ability" and "written language
        ability," and then each of these two categories are further divided into
        six distinct aspects of ability. Each category is rated on a one-to-ten
        scale, ten being the hypothetical peak of that aspect of ability.
      </p>

