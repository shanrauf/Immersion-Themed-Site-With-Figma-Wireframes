      <h3>Comprehension</h3>
      <p>
        The extent to which one can understand messages communicated through
        written language. Similar to Comprehension in spoken language, except
        "phoneme-parsing ability" (the ability to determine which vowels and
        consonants were actually spoken) is not relevant, so only "processing
        ability" (the ability to put together words in order to understand the
        message that is being communicated) is required.
      </p>

      <p>
        In written language, Comprehension can be thought of in terms of
        accuracy, speed, ease, and range. Native users of a language tend to
        understand very close to 100% of the language domains they regularly
        read in, so we could say that within the range of what they are
        comfortable with, native users’ accuracy is close to 100%. Native users
        also usually find that they can read in the language domains they are
        most comfortable in relatively quickly, and without a large amount of
        effort. Thus, we could also say that native users can read familiar
        kinds of language with a high level of speed and ease. Range refers to
        the spectrum of different language domains that can be understood, and
        is likely to differ significantly between native users. For example,
        someone who didn't graduate from high school may have a hard time
        understanding a philosophy thesis. Knowledge of concepts, as well as
        inferential ability (the ability to use what you do understand to fill
        in the gaps of what you don’t understand), are directly related to
        Comprehension ability.
      </p>

      <h3>Grammar</h3>
      <p>
        The extent to which the syntax used in writing is consistent with what
        is considered natural and correct by the majority of native users of the
        variety of the language that is being written. Largely the same as
        Grammar in Spoken Language Ability. Grammar is something that second
        language users tend to struggle with much more than native users, but
        most native users still slip up now and again. Instead of making
        spontaneous syntax errors due to getting tongue tied like in spoken
        language, when writing, native users often make spontaneous syntax
        errors due to typos, but are able to notice and self-correct these
        errors when re-reading what they wrote. Similar to spoken language, some
        native users will consistently break some generally accepted descriptive
        grammar rules. For example, although "me and Matt" may be consistent
        with some native speakers' grammar, those users would look uneducated if
        they used that instead of "Matt and I" in formal writing.
      </p>

      <h3>Phrasing</h3>
