        "He said a lie" is an error in Phrasing. But, a high level of
        development in Grammar is practically a prerequisite for reaching the
        higher end of the Phrasing spectrum, so making a strict distinction
        between the two can become difficult.
      </p>

      <h3>Hand Writing</h3>
      <p>
        The extent to which written Handwriting is legible and aesthetically
        pleasing. This category only applies when using a pen or pencil to write
        language out by hand. Judgements about Handwriting are largely
        subjective. Although having good handwriting was valued by many cultures
        in the past, due to the advent and proliferation of typing and digital
        text, in recent years its perceived value has waned immensely. Many
        people do not place any value on Handwriting when thinking about the big
        picture of writing ability.
      </p>

      <h3>Conventions</h3>
      <p>
        The extent to which the Conventions agreed-upon by native users of the
        variety of the language written are adhered to. "Conventions" include
        things like spelling and punctuation use. I have also placed the
        frequency of typos into this category. Technology has made it much
        easier to get by without having a detailed knowledge of Conventions.
        Judgements about competence in Conventions should be based on one's
        level when <em>not</em> relying on technology. For example, kanji
        knowledge in Japanese should be based on the ability to recall
        characters from Memory. Ability related to
        <em>reading and understanding</em> kanji would fall under Comprehension.
        Because technology is increasingly replacing knowledge of Conventions
        for many people, and Conventions are largely independent from the rest
        of Written Language Ability, many people may not place a lot of emphasis
        on Conventions when thinking about the big picture of writing ability.
      </p>

