        Phrasing is equally relevant to both native and second language users.
        At the lower end of the spectrum, Phrasing is about whether usage would
        be considered natural and correct by the majority of native users of the
        variety of the language written in. For example, "he said a lie,"
        although grammatically correct, would be considered unnatural by nearly
