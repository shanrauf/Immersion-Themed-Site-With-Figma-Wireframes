      <h3>Comprehension</h3>
      <p>
        The extent to which one can understand messages communicated through
        vocal and nonverbal language. Nonverbal language includes things like
        gestures and body language, which native speakers regularly use to
        communicate ideas both directly and indirectly. Comprehension is the one
        aspect of language ability that isn't related to producing language.
        Comprehension can be thought of in terms of accuracy, ease, and range.
        Native speakers tend to understand very close to 100% of the kinds of
        language that they are regularly exposed, so we could say that within
        the range of what they are comfortable with, their accuracy is close to
        100%. Native speakers also usually find that understanding the kinds of
        language that they are comfortable with doesn't require any effort; what
        is heard is automatically and instantaneously understood. So, we could
        also say that native speakers can understand familiar kinds of language
        with great ease. Range refers to the spectrum of different language
        domains that can be understood, and is likely to differ significantly
        between native speakers. For example, someone who didn't graduate from
        high school may have a hard time understanding a high-level lecture by a
        verbose college professor.
      </p>

      <p>
        Comprehension can also be thought of in terms of "phoneme-parsing
        ability" and "processing ability." "Phoneme-parsing ability" is the
        ability to determine which vowels and consonants (and by extension,
        which words) were actually spoken. For example, second language speakers
        often have a hard time making out what a fast-paced native speaker is
        saying, even though they might have no trouble understanding if they
        were given a written transcript of what was said. "Processing ability"
        is the ability to put together the words that are spoken in order to
        understand the message that is being communicated. In the example above,
        although the high school drop-out would be able to clearly hear what
        words the verbose college professor was saying, they still wouldn’t be
        able to comprehend the message, due to not having a large enough passive
        vocabulary and knowledge of concepts.. In this way, knowledge of
        concepts is directly related to Comprehension ability. Inferential
        ability (the ability to use what you do understand to fill in the gaps
        of what you don’t understand) can be considered part of "processing
        ability" as well.
      </p>

      <h3>Grammar</h3>
      <p>
        The extent to which the syntax used in speech is consistent with what is
        considered natural and correct by the majority of native speakers of the
        variety of the language that is being spoken. Grammar is something that
        second language speakers tend to struggle with much more than native
        speakers, but native speakers still slip up now and again. Native
        speakers will often make spontaneous grammatical errors due to getting
        tongue tied in the midst of conversation. In such cases, they will
        usually be fully aware of errors. Other times, native speakers will
        consistently break some generally accepted descriptive grammar rules.
        For example, although "me and Matt" may be consistent with some native
        speakers' grammar, those speakers may look uneducated if they used that
        instead of "Matt and I" in formal situations.
      </p>
      <h3>Phrasing</h3>
      <p>
        The extent to which words, phrases, and expressions are used skillfully
        in speech. Phrasing is equally relevant to both native and second
        language speakers. At the lower end of the spectrum, Phrasing is about
        whether usage would be considered natural and correct by the majority of
        native speakers of the variety of the language spoken. For example, "he
        said a lie," although grammatically correct, would be considered
        unnatural by nearly all native English speakers. Around the middle range
        of the spectrum, Phrasing becomes about using language that is
        appropriately tailored to the situation. For example, speaking with the
        right amount of formality in a business meeting. At the high end of the
        spectrum, Phrasing becomes about using language in exceptionally
        creative, clever, entertaining, elegant, or poetic ways. At the highest
        levels, Phrasing transitions into art. And as with all forms of art,
        value judgements become inherently subjective. At the lower end of the
        spectrum, distinguishing between Phrasing and Grammar is
        straightforward: Grammar is accuracy with regards to syntax rules, and
        Phrasing is accuracy with regards to word usage. "He told a lies" is an
        error in Grammar, whereas "He said a lie" is an error in Phrasing. But,
        a high level of development in Grammar is practically a prerequisite for
        reaching the higher end of the Phrasing spectrum, so making a strict
        distinction between the two can become difficult.
      </p>

      <h3>Pronunciation</h3>
      <p>
        The extent to which the timbre (vowels and consonants), pitch
        accent/stress accent/tones, and intonation of speech deviate from what
        is considered to be natural and correct by the majority of native
        speakers of the variety of the language spoken. Said another way,
        <em
          >when reading a text out loud, how close does the person sound to a
          native speaker?</em
        >
        Because Pronunciation is mainly a matter of not deviating from native
        speakers, it is most relevant to second language speakers. That said,
        many native speakers will still mispronounce words from time to time.
        For example, let's say a native speaker learns a word from reading and
        incorrectly guesses the spoken pronunciation and then uses that
        mispronunciation in speech. This could be an entirely idiosyncratic
        deviation from all other native speakers of the language. Native
        speakers with speech impediments may struggle with pronunciation as
        well.
      </p>

      <p>
        If multiple varieties of the language exist (for example, American
        English, British English, etc.), scores should be based on the amount of
        deviation from the specific variety of the language that the person’s
        speech most closely resembles. Speakers who are a perfect mix of
        multiple varieties of a language may be difficult to score.
      </p>
      <h3>Delivery</h3>
      <p>
        The extent to which the verbal performance and body language of a
        speaker either enhances, or subtracts from, the experience of listening
        to that speaker. Delivery is equally relevant to both native and second
        language speakers. Some examples of qualities that would subtract from a
        speaker's Delivery are stuttering, overuse of filler words, mumbling,
        hesitation, awkward or unnatural body language, a sense of being
        strained or tensed up, speaking too fast or too slow, and constantly
        restarting sentences mid-way through. Some examples of qualities that
        would enhance a speaker's Delivery are an engaging and easy to
        understand cadence, an ideal amount of enunciation, an appropriate
        amount of confidence, and a sense of flow or effortlessness.
      </p>

      <p>
        Delivery is independent from Pronunciation, Grammar, Phrasing, and
        Content. If two native speakers read the same text out loud, and one
        speaker was easier to understand and more pleasant to listen to than the
        other, that would be a difference in Delivery. Thought of another way,
        it is possible for a second language speaker to have poor Pronunciation,
        Grammar, and Phrasing, yet still have great Delivery.
      </p>
      <h3>Content</h3>
      <p>
        The extent to which messages verbally and nonverbally communicated are
        skillful. Whereas Phrasing is about <em>words</em>, Content is about
        <em>ideas</em>. Content is the most subjective category of language
        ability, as well as the category that most greatly diverges from what is
        traditionally thought of as "language ability." Content is equally
        relevant to both native and second language speakers.
      </p>

      <p>
        At the lower end of the spectrum, Content is about assimilating into a
        linguistically-bounded group of people through following cultural and
        societal norms. For example, in Japanese culture, accepting compliments
        is highly frowned upon; when complimented, playing down or denying the
        compliment is considered to be the appropriate reaction. Consistently
        complying with all these sorts of cultural norms (with the exception of
        intentionally breaking one in order to make a point) puts one at least
        at the mid-range of Content related ability. Development in this area
        will often have a greater influence on how a second language speaker is
        perceived by native speakers than improvements in the more technical
        aspects of language ability.
      </p>

      <p>
        At the high end of the spectrum, Content becomes about conveying
        messages that strongly influence other people. Some examples might be
        making people laugh, educating or informing, or providing encouragement
        or inspiration. The influence that a speaker exerts doesn't necessarily
        have to be <em>positive</em>, but it does have to be
        <em>intentional</em>. For example, accidently making someone laugh would
        not reflect a high level of development in Content.
      </p>

      <p>
        This high end of the Content spectrum covers an extremely broad range
        and is decidedly subjective. But, language is a tool to interact and
        communicate with other people, and so the ability to
        <em>produce language that affects listeners in </em>a desired<em>
          way</em
        >
        clearly must be taken into account. And because the actual Content that
        is communicated is undeniably the largest deciding factor in how
        language will affect someone, Content must be included in a
        comprehensive model of language ability.
      </p>
